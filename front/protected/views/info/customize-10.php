                        <div class="step">
                            <div class="stepnumber">
                                <img border="0" src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>Here is what you will need before you get started:</p>
                                <p>
                                    - to have <a href="https://www.eventbrite.com/signup">signed up for a free Eventbrite account</a> or <a href="https://www.eventbrite.com/login">logged in to your existing Eventbrite account</a> and <a href="/en_us/t/event-page-creation">created an event</a></p>
                                <p>
                                    - image-editing software (e.g., Adobe Photoshop, Adobe Illustrator, Snag-It or Skitch)</p>
                                <p>
                                    - an image-hosting site / account (we recommend <a href="http://tinypic.com/">TinyPic</a>)</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Create and correctly size your image</strong></p>
                                <p>
                                    In the image editor of your choice, create an image that is <strong>960 pixels wide</strong>, which is the width of your event page. (The height of the image can vary.) Here is an example (note that we've shrunk the example to fit this space):</p>
                                <p>
                                    <img width="592" height="117" border="0" src="http://blog.eventbrite.com/wp-content/uploads/2010/09/smphdr-956px.jpg" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Upload your image to an image-hosting site</strong></p>
                                <p>
                                    There are many image-hosting solutions to choose from. For this tutorial, we’ll use <a href="http://tinypic.com/">TinyPic</a>. Once you’ve uploaded your image, you’ll see a number of sharing options. Highlight the URL provided for the <strong>direct link</strong> and copy the URL to your clipboard.</p>
                                <p>
                                    <img border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/insert-image-into-custom-header-or-footer/direct_link.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Insert your image into the custom header or footer on your event page</strong></p>
                                <p>
                                    Back in Eventbrite, go to the <strong>Colors &amp; Options&nbsp;</strong>page for your event, if you’re not already there.</p>
                                <p>
                                    (To get there, click <strong>My Events </strong>at the top of the screen, then click <strong>Edit</strong>, under <strong>Quick Links</strong>, to the right of the title of your event. Next click <strong>Colors &amp; Options</strong> in the upper left.)</p>
                                <p>
                                    On the <strong>Colors &amp; Options</strong> page, click the <strong>Add HTML</strong> link.</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 612px; height: 184px; " src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/insert-image-into-custom-header-or-footer/custom_3.png" alt=""></p>
                                <p>
                                    Once the <strong>Add HTML</strong> window opens, enter the line of code shown in the image below, pasting the URL for your image in place of the URL between the quotation marks, as shown below. Remember to put quotations before and after your pasted URL.</p>
                                <p>
                                    <img width="506" height="350" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/insert-image-into-custom-header-or-footer/custom_4.png" alt=""></p>
                                <p>
                                    <strong>Note</strong>: You can edit the custom footer by clicking the <strong>Custom Footer</strong> link in the <strong>Add HTML </strong>window.</p>
                                <p>
                                    Click <strong>Save</strong> to close the <strong>Add HTML </strong>window when you’ve finished. Then click <strong>Save </strong>in the upper right of the <strong>Colors &amp; Options</strong>&nbsp;page. Well done!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Adjust the color scheme of your event page, if needed</strong></p>
                                <p>
                                    To customize the color scheme of your event page&mdash;e.g., to match the custom header and/or footer you just inserted&mdash;click <strong>modify colors</strong>.</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 614px; height: 189px; " src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/insert-image-into-custom-header-or-footer/custom_5.png" alt=""></p>
                                <p>
                                    To alter the color of a given element&mdash;such as the background or links&mdash;click on the colored square next the name of that element. In the pop-up window, move the vertical-sliding bar and/or color-selector circle within the color field until you find the exact hue for you. (Or you can enter the <a href="http://www.w3schools.com/html/html_colors.asp">hexadecimal value</a> for a color in the alphanumeric field.) The preview of your event page will update automatically.</p>
                                <p>
                                    <img width="347" height="294" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/insert-image-into-custom-header-or-footer/custom_6.png" alt=""></p>
                                <p>
                                    Click <strong>Save</strong> once you’ve made your color selection for an element.<strong> </strong>And then make sure to click <strong>Save </strong>in the upper right of the <strong>Colors &amp; Options</strong> page once you’re done!</p>
