                                <p>
                                    <strong>Does Eventbrite still charge a fee for orders that I refund?</strong></p>
                                <p>
                                    No. For an order that you completely refund, Eventbrite refunds all fees associated with that order.</p>
                                <p>
                                    Go <a href="/en_us/t/refund-an-order">here </a>to learn more about refunding an order in Eventbrite.</p>
