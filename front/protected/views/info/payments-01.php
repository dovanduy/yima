                            <div class="stepcontent">
                                <p>
                                    <strong>How much does it cost to use Eventbrite?</strong></p>
                                <p>
                                    First off, if your event is free--or you're just wanting to create a test event to try us on for size--Eventbrite is free to use! There are no monthly charges, enrollment or setup fees.</p>
                                <p>
                                    If you're charging for your event, you can learn more about the small per-ticket fee to use Eventbrite by going <a href="http://www.eventbrite.com/fees/">here</a>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>I'm charging for my event. Can I have my attendees pay Eventbrite's fee?</strong></p>
                                <p>
                                    Yes! By default, the per-ticket Eventbrite fee for paid events is added to the ticket price, and so is paid by the attendee. However, you can choose to pay the fees instead of your attendees.</p>
                                <p>
                                    Go <a href="/en_us/t/feehandling-options">here</a> to learn more about the fee-handling options that are available in Eventbrite.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>What payment-processing options do I have, and how much does each cost?</strong></p>
                                <p>
                                    You can choose from a number of different payment processors for your event, though we recommend our standard <a href="/en_us/t/credit-card-processing-faq">credit card processing</a> as the smoothest option for both you and your attendees.</p>
                                <p>
                                    For a comparison of the different payment-processing options, go <a href="/en_us/t/payment-processor-options">here</a>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>How can I offer offline (non-credit card) payment methods to my attendees? Does Eventbrite still charge a fee for offline payments? </strong></p>
                                <p class="stepcontent">
                                    To learn how to offer your attendees the option to pay offline, go <a href="/en_us/t/offline-payment-options">here</a>. The Eventbrite ticketing fee--though not the payment processing fee--is still charged for orders with offline payment. Go <a href="http://www.eventbrite.com/fees/">here </a>to learn more about Eventbrite's fees.</p>
                            </div>
