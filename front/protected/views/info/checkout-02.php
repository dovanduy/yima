                        <div class="block-heading">
                            <h3>Order confirmation customization</h3>
                            <ul>


                                <li>
                                    Provide answers to FAQ's like parking info, age restrictions and more</li>
                                <li>
                                    Disable PDF tickets for ticketless events</li>
                                <li>
                                    Send attendees to your website after completing an order</li>


                            </ul>
                        </div>
