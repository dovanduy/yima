                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> Use the <strong>Edit Order Confirmations</strong> feature to add custom information to the order confirmation page and the order confirmation email that attendees receive after registering for your event. This could include special instructions or <a href="/en_us/t/webinar-registration">webinar</a> log-in details. You can also turn off PDF tickets here--<strong><span style="color: #008000;">think green!</span></strong>--if your event doesn't require physical tickets.</p>
                                <p>
                                    We recommend including the following info in your order confirmations: your refund policy, age restrictions, lost ticket policy, parking information, door times, dress code, prohibited items, and/or your contact information.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To edit your order confirmations, from the <strong>My Events</strong> page, click on the title of your event to go to the <strong>Manage </strong>page for that event. On the <strong>Manage </strong>page, select <strong>Edit Order Confirmations</strong> from the features menu on the left side of the page.</p>
                                <p>
                                    <img title="Edit order confirmations" style="border-width: 0px; border-style: solid; width: 222px; height: 192px;" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/custom_order_conf/edit_order_conf.png" alt="Edit order confirmations"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Disable PDF tickets if your event does not require physical tickets:</strong></p>
                                <p>
                                    Un-check the two checkboxes at the top of the page. Turning off unnecessary PDF tickets can reduce both attendee confusion and paper waste. You can always print out a <a href="/en_us/t/print-checkin-list">check-in list</a>, use the <a href="/en_us/t/laptop-for-checkin">Entry Management tool</a> located in the Manage page for your event, or download and use our app for <a href="/en_us/t/iphone-for-checkin">iPhone</a> or <a href="/en_us/t/android-for-checkin">Android</a> to check in attendees at the door to your event.</p>
                                <p>
                                    <strong>Make sure to advise your attendees in the order confirmation email and/or page that tickets are not included and so not required. </strong></p>
                                <p>
                                    <img width="609" height="110" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/custom_order_conf/disable%20tickets.png" alt=""></p>
                                <p>
                                    Here's an example of the default PDF ticket that is attached to--and linked to in--the order confirmation email:</p>
                                <p>
                                    <img width="608" height="781" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/custom_order_conf/ticket.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Customize the order confirmation email:</strong></p>
                                <p>
                                    If you <strong>haven't </strong>disabled PDF tickets (see step 2 above), any text that you enter will appear on the PDF tickets that your attendees will receive and print <strong>and</strong> in the order confirmation email.</p>
                                <p>
                                    If you <strong>have </strong>disabled PDF tickets (see step 2 above), any text that you enter will appear in the <strong>Event Details</strong> section at the bottom of the order confirmation email. With tickets disabled, you have more formatting options for your text, and you can also enter HTML into your order confirmation email.</p>
                                <p>
                                    <img width="616" height="411" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/custom_order_conf/edit_order_conf_email.png" alt="How to customize the order confirmation email &amp;amp; page"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note:</strong> You can also change the default <strong>reply-to email address</strong> that will appear in the order confirmation email.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    Below is an example of a customized confirmation email that is sent to your attendees after they register. Notice the information we've added to the <strong>Event Details</strong> section at the bottom of the email.</p>
                                <p>
                                    <img border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/custom_order_conf/order_conf_email2.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Customize the order confirmation page:</strong></p>
                                <p>
                                    Enter any information that you would like to appear in the <strong>Message </strong>section of the order confirmation page, which is the page where an attendee lands after successfully completing an order. This section reads HTML, allowing you to add links and images to your order confirmation page.</p>
                                <p>
                                    <img width="614" height="398" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/custom_order_conf/edit_order_confirmation_page.png" alt=""></p>
                                <p>
                                    Below is an example of the default order confirmation page that your attendees are taken to after successfully completing their order. You can customize this page, as described above.</p>
                                <p>
                                    <img width="608" height="594" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/custom_order_conf/order_conf_page3.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div id="redirect" class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> If you want the page to just automatically redirect to your own website once an attendee is finished registering, clear all of the text and just enter your website URL (including the <strong>http://</strong>).</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    Make sure to click <strong>Save Changes</strong> when you're done!</p>
                            </div>
                        </div>
