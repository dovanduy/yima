                        <div class="block-heading">
                            <h3>Email reminders for already-registered attendees</h3>
                            <ul>
                                <li>
                                    Remind all or some of your attendees of your upcoming event</li>
                                <li>
                                    Great for FAQs like restrictions and transit info</li>
                            </ul>
                        </div>

                        <!--step-->
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> You are able to email only those who have already registered for your event. If you would like to send an email to those invited, but not yet registered, feel free to send out additional <a href="/en_us/t/email-invitations">email invitations</a>.</p>
                                <p>
                                    By default, Eventbrite sends a 48-hour reminder email to your attendees. While you cannot edit its text, you can delete this automated reminder and replace it with a reminder email of your own design, as detailed below. To disable these automated 48-hour reminder emails for all events in your account, please <a href="http://www.eventbrite.com/contact-us/">contact us</a>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    From the <strong>My Events</strong> page, click on the name of an event to go to the <strong>Manage </strong>page for that event. On the <strong>Manage</strong> page, select <strong>Email Attendees</strong> from the features menu on the left side of the page.</p>
                                <p>
                                    <img alt="" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/howtoemailattendees/sidebar.png" border="0"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Click the <strong>Create New Attendee Email</strong> button to create a new message.<img alt="" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/howtoemailattendees/e-mail_attendee_step_2.png" border="0" height="168" width="493"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Customize the<strong> Name</strong> and <strong>Reply-To Email</strong> fields to control who will be shown as the sender of your email and to which email address any replies to your email will go. Then select the recipients of this email using the <strong>To</strong> dropdown menu.</p>
                                <p>
                                    <img alt="" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/howtoemailattendees/e-mail_attendee_step_3.png" border="0" height="349" width="509"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    Customize the <strong>Subject</strong> and <strong>Message</strong> fields of the email. You can include a <strong>link to print tickets</strong> in your email (if <a href="/en_us/t/order-confirmation-customization">PDF tickets are enabled</a> for the event), as well as include a <strong>link to invite friends to the event</strong>, if your event is public.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> If you have any files--e.g., slides or a presentation--to send to your recipients, you can upload these files to an outside site and then include in your email a link to download the files. Unfortunately, it isn't possible to attach files directly to an email to your attendees.</p>
                                <p>
                                    <img alt="" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-schedule-and-send-email-reminders/message.png" border="0" height="569" width="607"></p>
                                <p>
                                    <img alt="" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/howtoemailattendees/e-mail_attendee_step_4.png" border="0" height="685" width="493"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    Choose <strong>when </strong>you would like to send the email. Your options are to send now, at a specific date/time, or a certain number of days/hours/minutes before the start of your event. Then click <strong>Send Now</strong> or <strong>Save</strong>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>More Options:</strong></p>
                                <p>
                                    You can easily copy and resend a scheduled or sent email by clicking <strong>Copy</strong> next to the subject of that email and following the steps above, making any edits you would like along the way.</p>
                                <p>
                                    <img alt="" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/howtoemailattendees/e-mail_attendees_step_5.png" border="0" height="179" width="468"></p>
                            </div>
