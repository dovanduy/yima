                                <p>
                                    <strong>Tip:</strong> In order to use the <strong>Print Name Badges</strong> feature, you must have <a href="http://get.adobe.com/flashplayer/">Adobe Flash</a> and <a href="http://get.adobe.com/reader/">Adobe Reader</a> installed on your computer.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To access the name badge tool, from the <strong>My Events</strong> page, click on the title of your event to go to the <strong>Manage </strong>page for that event. On the <strong>Manage</strong> page, select <strong>Print Name Badges</strong> from the features menu on the left side of the page. <img border="0" title="Print Name Badges" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/print_badges/features%20menu.png" alt="Print Name Badges"></p>
                            </div>
                            <p>
                                &nbsp;</p>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Choose the set of attendees for whom you would like to print badges. You can choose <strong>All Unique Attendees, Specific Attendees</strong> (choose names from a list), or <strong>Attendees by Ticket Type </strong>(attendees who registered for a particular ticket type). <img border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/print_badges/select%20attendees.png" alt="How to customize and print name badges"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Then choose from a list of the available commonly used <strong>Avery </strong>badge label templates. <img width="611" height="207" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/print_badges/badge%20style.png" alt="How to customize and print name badges"></p>
                            </div>
                            <p>
                                &nbsp;</p>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>For more information about the different label/badge styles and to order online, go to the <a href="http://www.avery.com/">Avery website</a>.</p>
                            </div>
                        </div>
