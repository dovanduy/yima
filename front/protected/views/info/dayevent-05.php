                                    Customize the layout of the name badge by choosing which information should appear where on the badge. You can also change the font and alignment. <img width="609" height="171" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/print_badges/customize%20layout.png" alt="How to customize and print name badges"></p>
