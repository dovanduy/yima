                        <p class="center">
                            <span style="font-size: medium;"><strong>How to issue a refund for an attendee or order in your account:</strong></span></p>

                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    From the <strong>My Events</strong> page, click on the title of your event to go to the <strong>Manage </strong>page for that event. On the <strong>Manage </strong>page, select <strong>Event Reports</strong> from the features menu on the left side of the page.</p>
                                <p>
                                    <img width="231" height="199" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/refund_attendee/event%20reports22.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Set the <strong>Report Type </strong>dropdown menu to <strong>Order Details. </strong>Find the order you would like to refund in the <strong>Search for Orders</strong> field by order number, name or email address.</p>
                                <p>
                                    <strong><img width="611" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/refund_attendee/howtorefund1.png" alt=""></strong></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Select <strong>Refund this Order</strong> from the top <strong>Quick Actions</strong> menu located on the right side of the gray order box.</p>
                                <p>
                                    <img width="611" height="258" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/refund_attendee/howtorefund2.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img border="0" src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note:</strong> If you are using <strong>PayPal</strong> or <strong>Google Checkout</strong> for payment processing, you'll be redirected to that site to complete the refund (and you can skip to step 5 below). For more information on processing refunds through PayPal, click <a title="here" href="https://www.paypal.com/cgi-bin/webscr?cmd=xpt/Marketing/bizui/SendRefund-outside">here</a>. For more information on processing refunds through Google Checkout, click <a title="here" href="http://checkout.google.com/support/sell/bin/answer.py?answer=134379">here</a>. Any refunds that you initiate in your PayPal or Google Checkout account will automatically update the corresponding order in your Eventbrite account!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4a</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>To issue a complete refund on an order:</strong></p>
                                <img width="611" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/refund_attendee/howtorefund3.png" alt=""><p>
                                    Find the attendee you would like to refund and select the <strong>Refund Quantity</strong> from the dropdown menu. The refund amount should automatically fill with the <strong>Total Refund</strong> amount. Select <strong>Continue</strong> to go to the next screen, where you can review the refund before it is processed.</p>
                                <p>
                                    On the next screen, you can leave a message in the <strong>Note to Buyer</strong> text field. This message will appear in the email that the attendee receives when the order is refunded.</p>
                                <img width="611" height="372" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/refund_attendee/howtorefund4.png" alt=""><p>
                                    If you need to make any changes, click the <strong>Cancel</strong> button and restart the process. If you are set to refund, go ahead and click the <strong>Issue Refund </strong>button!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4b</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>To issue a partial refund on an order:</strong></p>
                                <img width="611" height="440" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/refund_attendee/howtorefund5.png" alt=""><p>
                                    Select a <strong>Refund Quantity </strong>of 1. In the <strong>Refund Amount</strong> field, enter the amount you would like to partially refund. The <strong>Total Refund</strong> amount will adjust to the amount entered above.</p>
                                <p>
                                    Click <strong>Continue</strong> to issue the partial refund.</p>
                                <p>
                                    Like a full refund, you can review the refund before it is processed. To finalize the refund click <strong>Issue Refund</strong>!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    Once you have completed the refund, the <strong>Order Details</strong> report and <strong>Attendee Summary</strong> for your event will be automatically updated. Your refunded attendees/orders will be viewable in the <strong>Refunded/Canceled</strong> orders list on the <strong>Order Details</strong> report.</p>
                                <p>
                                    A full refund will appear as follows:</p>
                                <img width="619" height="447" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/refund_attendee/full_refund_order_report.png" alt=""><p>
                                    A partial refund will appear as follows:</p>
                                <img width="611" height="489" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/refund_attendee/howtorefund6.png" alt="">
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img border="0" src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>Don't see the option to refund an order in the top <strong>Quick Actions</strong> dropdown menu?</p>
                                <p>
                                    If the order was placed more than six months ago, you'll need to send your attendee an offline refund (e.g., a check), as our payment gateway does not store credit card info for more than six months.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img border="0" src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>By default, the attendee will be able to click on a <a href="/en_us/t/can-i-get-a-refund">Request Refund</a> link when they log in to their Eventbrite account to manage their order for your event. You will receive an email requesting the refund. You can disable this feature by clicking on&nbsp;<strong>Customize Order Form</strong> found on the left side of your event's <strong>Manage</strong> page.</p>
                                <img src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_survey/customizeorderold.png" alt=""><p>
                                    Scroll down to the <strong>Optional Settings</strong> section and uncheck the box next to <strong>Return Policy</strong> and click <strong>Save Changes</strong>.</p>
                                <img width="607" height="471" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/refund_attendee/refundpolicy2.png" alt=""><p>
                                    You've done it!&nbsp; Your attendees will not see the <strong>Request Refund</strong> link when they log in to their account. We do recommend placing a refund policy on your event page so that your attendees are aware of your policy.</p>
                            </div>
