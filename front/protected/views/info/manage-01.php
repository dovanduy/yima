                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>To view the usage of all your discount and/or access codes in one place, you'll want to run an <strong>Attendee Summary</strong> report. Learn how to do that <a href="/en_us/t/attendance-summary">here</a>! And to see how an attendee would enter a discount or access code when ordering, click <a href="/en_us/t/apply-a-discount-code">here</a>.</p>
