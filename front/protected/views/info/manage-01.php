                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>Promotional codes are <strong>not</strong> case sensitive. This means that your attendees can enter in the discount or access code you provide to them without worrying about getting the capitalization of the code exactly right!</p>
                                <p>
                                    An attendee can only use one discount or access code per order. If you'd like to offer access code users a discount, just set the price of the access code-protected hidden ticket to the discounted amount!</p>
                                <p>
                                    Now you can bulk upload up to 3,000 discount or access codes in a single .csv or .txt file!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To create or manage your discount and/or access codes, first <a href="https://www.eventbrite.com/signup">sign up</a> for a free Eventbrite account or<a href="https://www.eventbrite.com/login"> log in</a> to your existing Eventbrite account and<a href="/en_us/t/event-page-creation"> create an event</a>.</p>
                                <p>
                                    For an already-created event, click <strong>My Events</strong> at the top of the screen and then click on the name of your event to go to that event's <strong>Manage</strong> page.</p>
                                <p>
                                    <img width="613" height="272" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_discount_code/2manage.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    On your event's <strong>Manage </strong>page, select <strong>Create Promotional Codes</strong> from the features menu on the left side of the page.</p>
                                <br><p>
                                    <img width="229" height="173" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_discount_code/2features2.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3a</div>
                            <div class="stepcontent">
                                <p>
                                    To create a <strong>discount code</strong>, first select <strong>Discount</strong> from the <strong>Code Type </strong>dropdown menu. (If you'd like to create an access code, go to step 3b below.)</p>
                                <p>
                                    <img width="615" height="148" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_discount_code/2discount2.png" alt=""></p>
                                <p>
                                    Then enter the following information and click <strong>Save </strong>once you're finished:</p>
                                <p>
                                    -<strong> Code</strong> - This is the discount code that you'll provide to your attendees to use when they order. No two discount or access codes can be exactly the same; all codes must be unique.&nbsp;</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> If you have many discount codes to create that will all have the same attributes (<strong>discount amount</strong>, <strong>uses</strong>, etc.), feel free to put all those codes into a .txt or .csv file (the latter using Microsoft Excel) and then upload the file into our system. Once you've bulk uploaded the list of codes, you can then set the attributes for all of these codes in Eventbrite, as detailed directly below.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    -<strong> Discount Amount </strong>- The discount can either be a fixed amount, or a percentage off the ticket price.</p>
                                <p>
                                    -<strong> Applies To</strong> - Which of your ticket types this discount will apply to.</p>
                                <p>
                                    -<strong> Uses</strong> - The total number of tickets this discount code can be applied to.</p>
                                <p>
                                    -<strong> Starts</strong> - When this discount code will become valid.</p>
                                <p>
                                    -<strong> Ends</strong> - Until when this discount code will be valid.</p>
                                <p>
                                    <img width="610" height="728" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_discount_code/2discount22.png" alt="How to create and manage discount codes"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> Once you've created and saved a discount code, you can always click the name of the discount code while on the main <strong>Create Promotional Codes</strong> screen to see the details for that code. At the bottom of a particular <strong>Edit discount code</strong> screen, you'll see a link/URL with that discount code embedded in it. Anyone clicking this link will be taken to your event page, where the discount code will be auto-filled and ticket price(s) already reduced. You can provide this link to your attendees to make it even easier for them to register at the discounted price!</p>
                                <p>
                                    <img width="613" height="152" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_discount_code/2URL23.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3b</div>
                            <div class="stepcontent">
                                <p>
                                    To create an <strong>access code</strong>, first select <strong>Access</strong> from the <strong>Code Type </strong>dropdown menu. (If you'd like to create a discount code, go to step 3a above.)</p>
                                <p>
                                    <img width="617" height="151" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_discount_code/2access.png" alt=""></p>
                                <p>
                                    Then enter the following information and click <strong>Save </strong>once you're finished:</p>
                                <p>
                                    -<strong> Code</strong> - This is the access code that you'll provide to your attendees to enter before ordering to make the hidden ticket type appear. No two discount or access codes can be exactly the same; all codes must be unique.&nbsp;</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> If you have many access codes to create that will all have the same attributes (<strong>discount amount</strong>, <strong>uses</strong>, etc.), feel free to put all those codes into a .txt or .csv file (the latter using Microsoft Excel) and then upload the file into our system. Once you've bulk uploaded the list of codes, you can then set the attributes for all of these codes in Eventbrite, as detailed directly below.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    -<strong> Applies To</strong> - Which of your <strong>hidden</strong> ticket types this access code will apply to.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note:</strong> Access codes will only work with <strong>hidden</strong> ticket types. You can hide a ticket type by clicking the <strong>Edit</strong> tab in the upper left, and then clicking <strong>Hide</strong> (or selecting <strong>Hidden</strong> from the <strong>Sales Status</strong> dropdown menu, if you're using our <a href="/en_us/t/more-info-about-our-new-design">new design</a>) next to the name of that ticket type. Once you've hidden the relevant ticket(s), return to the <strong>Create Promotional Codes</strong> screen to set up your access code(s). <strong> </strong></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    -<strong> Uses</strong> - The total number of tickets this discount code can be applied to.</p>
                                <p>
                                    -<strong> Starts</strong> - When this discount code will become valid.</p>
                                <p>
                                    -<strong> Ends</strong> - Until when this discount code will be valid.</p>
                                <p>
                                    <img width="609" height="685" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_discount_code/2access22.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> Once you've created and saved an access code, you can always click the name of the access code while on the main <strong>Create Promotional Codes</strong> screen to see the details for that code. At the bottom of a particular <strong>Edit access code</strong> screen, you'll see a link/URL with that access code embedded in it. Anyone clicking this link will be taken to your event page, where the access code will be auto-filled and the hidden ticket type will already be showing. You can provide this link to the relevant people to make it even easier for them to register for the hidden ticket type!</p>
                                <p>
                                    <img width="608" height="152" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_discount_code/2URL24.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    You can view, edit or delete any of the discount or access codes you've created by clicking on <strong>Create Promotional Codes</strong> on the left side of your event's <strong>Manage</strong> page at any time, as detailed in step 2 above.</p>
                                <p>
                                    <img width="618" height="350" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_discount_code/2edit.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    To track the usage of a particular discount or promotional code, first click <strong>Event Reports</strong> on the features menu on the left side of your event's <strong>Manage</strong> page.</p>
                                <p>
                                    <img width="231" height="199" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_discount_code/event%20reports22.png" alt=""></p>
                                <p>
                                    Filter the <strong>Report Type</strong> dropdown menu to <strong>Promotional code usage</strong>. Then select the particular discount or access code you'd like to view from the <strong>Promotional code</strong> dropdown menu.</p>
                                <p>
                                    If you're looking at a discount code, you'll see that code and the discount percentage or amount shown in the <strong>Code</strong> column. (If you're looking at an access code, the <strong>Code</strong> column will be blank.)</p>
                                <p>
                                    <img width="614" height="330" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_discount_code/2reports2.png" alt=""></p>
                            </div>
                        </div>
