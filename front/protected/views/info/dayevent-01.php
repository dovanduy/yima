                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Important Note: </strong>At this time, you can only sell tickets using At The Door for non-<a href="/en_us/t/repeating-events">repeating events</a> priced in USD that are using our default <a href="/en_us/t/credit-card-processing-faq">credit card processing</a>.</p>
                                <p>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <strong><span style="font-size: large;">Create At The Door ticket types</span></strong><br>
                            &nbsp;</p>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To get started, first <a href="http://www.eventbrite.com/signup">sign up</a> for a free Eventbrite account or <a href="http://www.eventbrite.com/login">log in</a> to your existing Eventbrite account and <a href="http://www.eventbrite.com/create">create an event</a>.</p>
                                <p>
                                    If you’ve already created an event, click <strong>My Events</strong> at the top of the screen. Then click on <strong>Edit</strong> to the right of your event’s name to go to the <strong>Event Details</strong> page.<br>
                                    <img width="596" height="258" title="How to create ticket types" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/atd-ipad-app/atdedit.png" alt="How to create ticket types"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>You must be using our <a href="/en_us/t/more-info-about-our-new-design">new design</a> in order to create At The Door ticket types, and then use our At The Door app to sell tickets on your iPad at your event.&nbsp;</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    On your event's <strong>Event Details</strong> page, go to <strong>Step 2: Create Tickets</strong> to create an <strong>At The Door</strong> ticket type.</p>
                                <p>
                                    Click the <strong>Add Ticket</strong> button to create a new <strong>Paid</strong> or <strong>Free</strong> ticket type.&nbsp;</p>
                                <p>
                                    Enter the <strong>Ticket Name</strong>, <strong>Ticket Quantit</strong><strong>y</strong>, and <strong>Ticket Price </strong>for that ticket type.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Select <strong>At The Door - On Sale</strong> from the <strong>Sales Status</strong> dropdown menu for that ticket type, as shown in the image below. <img width="598" height="286" title="How to create ticket types" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/atd-ipad-app/ATD1.png" alt="How to create ticket types"></p>
                                <p>
                                    Click <strong>Save</strong> in the upper right to save your changes.</p>
                                <p>
                                    Ticket types with a <strong>Sales Status</strong> of <strong>At The Door - On Sale</strong> (or <strong>At The Door - Hidden</strong>) will not be viewable on your event page online. (To sell the same ticket on your online event page, you must create a ticket type of the same name, but choose <strong>On Sale</strong> from the <strong>Sales Status</strong> dropdown menu.)</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note:</strong> If you want to hide your At The Door ticket type so it doesn't appear on the iPad app--e.g., if you're having a two-day event and you want to hide the second day's ticket type(s)--select&nbsp;<strong>At The Door - Hidden</strong> from the <strong>Sales Status </strong>column for that ticket type.</p>
                                <p>
                                    To make the ticket type appear later, select <strong>At The Door - On Sale</strong> from the <strong>Sales Status </strong>dropdown menu and save your changes.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    Repeat step #2 above to create multiple ticket types to sell on your iPad using At The Door. Once you've created all of your <strong>At the Door</strong> ticket types, you're ready to start selling tickets on your iPad!</p>
                                <p>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <strong><span style="font-size: large;">Get started with At The Door</span></strong><br>
                            &nbsp;</p>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    To sell tickets on-site at your event, download our At The Door iPad app <a href="http://itunes.apple.com/us/app/at-the-door/id488236441?ls=1&amp;amp;mt=8">here</a>.</p>
                                <p>
                                    On your iPad, tap on the <strong>At the Door</strong> app icon and log in using the same email and password that you use to log in to your Eventbrite account.</p>
                                <p style="text-align: center;">
                                    <img width="500" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/atd-ipad-app/atd-login1.png" alt=""></p>
                                <p>
                                    Once logged in, tap the calendar icon in the lower left and select the event that you want to sell tickets to--and you're ready to sell!</p>
                                <p>
                                    (Only events with <strong>At The Door</strong> ticket types will appear in the app. If your event doesn't appear, be sure to create at least one <strong>At The Door</strong> ticket type by following the instructions beginning with step #1 above.)</p>
                                <p style="text-align: center;">
                                    <img width="500" height="415" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/atd-ipad-app/atd-findevent.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>To control <strong> </strong><a href="/en_us/t/multiuser-account-access">multi-user (subuser) access</a> to At The Door, first log in to the admin account on Eventbrite.com. Click <strong>Account</strong> in the upper right, then click <strong>Multi-User Access</strong>. Click&nbsp;<strong>Edit</strong> next to a subuser's email address and, in the <strong>Actions this user can perform</strong> section, check the boxes next to <strong>Access Eventbrite At The Door</strong> and <strong>Manage orders and attendees</strong>.</p>
                                <p>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <strong><span style="font-size: large;">Sell tickets for cash or credit</span></strong><br>
                            &nbsp;</p>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    On the main <strong>Sell Tickets </strong>screen in the iPad app, simply tap the<strong> ticket buttons</strong> (e.g., <strong>General Admission</strong>, <strong>VIP</strong> or <strong>Und</strong><strong>er 21</strong> in the image below) to add those ticket types to your cart. You can tap the buttons multiple times to add multiple tickets to the order, or you can add and subtract tickets by using the <strong>+</strong>&nbsp;and <strong>-</strong> buttons next to the ticket quantity.&nbsp;</p>
                                <p style="text-align: center;">
                                    <img width="500" height="415" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/atd-ipad-app/atd-addtickets.png" alt=""></p>
                                <p>
                                    Tap the <strong>Cash</strong> button in the upper right to process a cash sale. Enter the amount tendered to display the change that's due, or just fly through by tapping <strong>Paid</strong> without entering anything.<strong>&nbsp;</strong></p>
                                <p style="text-align: center;">
                                    <strong><img width="500" height="415" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/atd-ipad-app/atd-paycash.png" alt=""></strong></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note:&nbsp;</strong>Cash sales processed using At The Door are&nbsp;<strong>not</strong> charged a fee--they are <strong>completely free</strong>!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    If an attendee is using a credit card to pay, tap the <strong>Credit</strong> button and enter the attendee’s credit card information using the on-screen keypad. Once you've entered all the required information (number, expiration date, CVV), tap <strong>Process</strong>.&nbsp;</p>
                                <p>
                                    To swipe a credit card instead of keying in the card number, check out our <a href="/en_us/t/credit-card-reader-for-ipad">At The Door Card Reader</a>!</p>
                                <p style="text-align: center;">
                                    <img width="500" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/atd-ipad-app/atd-processcard.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>Credit card orders processed using At The Door are <strong>not</strong> charged the 2.5% + $0.99 Eventbrite fee, only the 3% credit card processing fee. This credit card processing fee will be included in the total, and cannot be passed on to the buyer--this is a legal requirement.</p>
                                <p>
                                    If the buyer’s total is more than $25, they’ll be asked to sign on the iPad using their finger. After they sign, tap <strong>Confirm</strong> to process the payment.</p>
                                <p style="text-align: center;">
                                    <img width="500" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/atd-ipad-app/atd-signbelow.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>If you're processing an order that includes only free tickets, tap the&nbsp;<strong>Assign</strong> button in the upper right to finish processing the order.&nbsp;</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>Transactions cannot be processed without an Internet connection. If you lose Internet connectivity while on the <strong>Sell</strong> <strong>Tickets</strong> screen, the <strong>ticket buttons</strong> will become untappable until the Internet connection is restored. If you lose Internet connectivity in the middle of a transaction, an <strong>alert</strong> will pop up to notify you.</p>
                                <p>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <strong><span style="font-size: large;">Collect attendee information</span></strong></p>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    After taking payment, you have the option to capture the buyer’s name and email. This information will appear on their order and in all reporting on Eventbrite.com.</p>
                                <p style="text-align: center;">
                                    <img width="500" height="415" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/atd-ipad-app/atd-collectinfo.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>If you’d prefer not to collect this information, go to <strong>Settings</strong>, found on the left sidebar of the app,<strong> </strong>to turn off this feature.</p>
                                <p>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <strong><span style="font-size: large;">Email or print receipt</span></strong><br>
                            &nbsp;</p>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    If you didn’t collect the attendee’s email for your own purposes, you can still send them an <strong>email receipt</strong>. This email isn't saved; it is just used to send the receipt. &nbsp;</p>
                                <p style="text-align: center;">
                                    <strong><img width="500" height="415" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/atd-ipad-app/atd-receipt.png" alt=""></strong></p>
                                <p>
                                    To learn how to print tickets and/or a receipt from a supported printer, go <a href="/en_us/t/print-tickets-and-receipts-with-ipad">here</a>.</p>
