                                1</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Why is the Who’s Going module enabled by default for all events?&nbsp;</strong><br>
                                    Events are naturally social, and people already decide whether to attend an event based on which of their friends are attending. See Who’s Going just surfaces the information that many attendees already want to see. We think this feature makes for richer event pages, and expect it to become a core part of the Eventbrite experience.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Can I move where the "Who's Going" module displays on my event page?&nbsp;</strong><br>
                                    Like the other modules on our event pages, Who’s Going is fixed in a particular place based on our user experience research, design and quantitative testing. The Who’s Going module is fixed below the ticket/registration module so that it doesn’t interfere with the purchase flow, and is also constrained to only show two rows of users at first so that the Event Details module isn’t pushed too far down the page&nbsp;(Attendees can choose to expand the Who’s Going module if more than six of their friends are going).</p>
