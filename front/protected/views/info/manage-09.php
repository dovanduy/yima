                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To get started, first <a href="https://www.eventbrite.com/signup">sign up</a> for a free Eventbrite account or<a href="https://www.eventbrite.com/login"> log in</a> to your existing Eventbrite account and<a href="/en_us/t/event-page-creation"> create an event</a>.<br>
                                    If you’d like to edit an already-created event, click <strong>My Events</strong> at the top of the screen and then click on <strong>Edit </strong>to the right of your event’s name.</p>
                                <p>
                                    <img width="618" height="265" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/collect_donations/MyEvents.png" alt=""></p>
                            </div>
