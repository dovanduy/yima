                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    In <strong>Step 2: Create Tickets</strong>, select <strong>Donation </strong>from the <strong>Add Ticket Type </strong>dropdown menu<strong>.</strong> You can set the <strong>Ticket Quantity </strong>for this type of ticket, which will control how many individual donations you will be able to accept. Or you can leave this field blank to collect an unlimited number of donations.</p>
                                <p>
                                    <img width="614" height="214" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/collect_donations/donation.png" alt="How to collect donations on the event page"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    You can also edit a number of additional options, which are accessed by clicking <strong>Settings</strong> for this ticket type:</p>
                                <p>
                                    <img width="615" height="364" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/collect_donations/settings.png" alt="How to collect donations on the event page"></p>
                                <p>
                                    <strong>- Ticket Description</strong>: Enter more details about this ticket type here. You could include a suggested minimum (or maximum) donation, or tell what donated funds will be used for. What you enter will appear as smaller text below the name of your ticket type on your event page. You have the option to <strong>Hide the description on your event page</strong>, so that an attendee must click the <strong>more info</strong> link to view the description.</p>
                                <p>
                                    <strong>- Start Sales</strong>: You can choose to start the sales of this ticket type at a particular date/time, an amount of time before the event starts, or when sales end for another of your ticket types (if you have created more than one ticket type).</p>
                                <p>
                                    <strong>- End Sales</strong>: You can choose to end the sales of this ticket type at a particular date/time or an amount of time before the event starts.</p>
                                <p>
                                    <strong>- Service Fees</strong>: Choose how you want to handle any <a href="http://www.eventbrite.com/fees/">fees</a>. For donation ticket types, we recommend selecting <strong>Absorb the fees into the ticket price</strong>, so that the attendee donating is charged exactly the amount they choose when registering. Learn more <a href="/en_us/t/feehandling-options">here</a> about how and why to choose who pays the fees.</p>
