                        <p class="center">
                            <span style="font-size: small;"><strong>DISABLE PDF TICKETS FOR YOUR EVENT</strong></span><br>
                            &nbsp;</p>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To get started, first <a href="https://www.eventbrite.com/signup">sign up</a> for a free Eventbrite account or<a href="https://www.eventbrite.com/login"> log in</a> to your existing Eventbrite account and<a href="/en_us/t/event-page-creation"> create an event</a>.</p>
                                <p>
                                    To disable PDF tickets for an already-created event, go to your event's <strong>Manage</strong> page. You can get there by clicking on <strong>My Events</strong> at the top of the screen, and then clicking on the name of your event.</p>
                                <p>
                                    <img width="621" height="279" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/best-practices-for-ticketless-events/manage.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Once you've published or made your event live, go to your event's <strong>Manage</strong> page.&nbsp;Click on&nbsp;<strong>Edit Order Confirmations</strong>, found on the features menu on the left side of the page.</p>
                                <p>
                                    <img width="229" height="129" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/best-practices-for-ticketless-events/features.png" alt=""></p>
                                <p>
                                    On the next screen, uncheck the two checkboxes near the top of the page: <strong>Attach PDF tickets in confirmation email</strong> and <strong>Include Print Tickets link in confirmation email</strong>.</p>
                                <p>
                                    In the <strong>Customize the order confirmation email </strong>text box, make sure to advise your attendees that tickets won't be issued for the event. We recommend that you also copy and paste this message into the <strong>Customize the order confirmation page</strong> text box below that.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img border="0" src="http://i392.photobucket.com/albums/pp8/postdesign/eventbrite/icon_note.gif" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>If you're holding a webinar or other online event, you'll want to include all the log-in instructions for your attendees here. Be sure to include a phone number where you can be reached, in case your attendees have trouble logging in.</p>
                                <p>
                                    <img width="617" height="726" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/best-practices-for-ticketless-events/order%20conf.png" alt=""></p>
                            </div>
