                                    Once you've&nbsp;<a href="/en_us/t/event-page-creation">created an event</a> and published or made it live, create and schedule a reminder email for your attendees. Be sure to include all the information you've provided in the order confirmation page and email--as detailed in step 2 above--so your attendees are fully informed!</p>
