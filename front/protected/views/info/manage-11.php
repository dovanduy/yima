                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To get started, first <a href="http://www.eventbrite.com/signup">sign up</a> for a free Eventbrite account or <a href="http://www.eventbrite.com/login">log in</a> to your existing Eventbrite account and <a href="http://www.eventbrite.com/create">create an event</a>.</p>
                                <p>
                                    If you’d like to edit an already-created event, click <strong>My Events</strong> at the top of the screen and then click on <strong>Edit</strong> to the right of your event’s name.<br><img width="592" height="177" border="0" title="How to create ticket types" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_ticket/myevents1.png" alt="How to create ticket types"></p>
