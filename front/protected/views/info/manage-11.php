                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Now, you’ll want to click on the <strong>Settings</strong> link across from the <strong>Ticket Name</strong> so you can set the number of tickets included in the package.</p>
                                <p>
                                    <img width="600" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-create-a-group-ticket/group-ticket-new3.png" alt=""></p>
                                <p>
                                    <br>
                                    Enter the number of tickets included in the package in both the <strong>Minimum</strong> and <strong>Maximum</strong> fields for the <strong>Tickets permitted per order</strong>. Click Save at the top right of the page to save your changes.</p>
                                <p>
                                    <img width="602" height="427" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-create-a-group-ticket/group-ticket-new33.png" alt=""></p>
