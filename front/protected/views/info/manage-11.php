                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    In <strong>Step 2: Create Tickets</strong>, enter the <strong>Ticket Name</strong> (describing what your attendees are ordering), <strong>Ticket Quantity</strong>, and <strong>Ticket Price</strong> in the appropriate fields. If you’ve already created tickets, you’ll want to click on the <strong>Add Ticket Type</strong> button to open up a new ticket then fill out the necessary fields.</p>
                                <p>
                                    <img width="597" height="240" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-create-a-group-ticket/group-ticket-new2.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>Keep in mind that the price you set for your ticket will be multiplied by the number of tickets in the group or package, to equal the total price for that group or package.</p>
                                <p>
                                    E.g.: If a ticket is regularly priced at $20--so, regularly $100 for five tickets--and you'd like to give a $35 discount to make the price for 5 tickets total to $65, you'll want to divide $65 by 5. This comes to $13 per ticket. This means you'll want to set the price of the ticket to $13.</p>
                            </div>
                        </div>
                        <div class="step">
