                        <p style="text-align: center;">
                            <span style="font-size: medium;"><strong><strong>Have questions? Here are some handy FAQs about our new design.</strong> </strong></span></p>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <br>
                                <p>
                                    <strong>+ Why did Eventbrite build a new Create page?</strong></p>
                                <p style="padding-left: 30px;">
                                    The old version of the Create page is a few years old, and we’ve received lots of great feedback from our event organizers about how we can improve the experience and functionality of this page. The new Create page is the result of up-front design research and usability sessions conducted with many of our users, and we’ve incorporated your feedback into our product. The new design repackages all the key functionality from the old page and offers several new features in a very user-friendly manner, which will simplify the event-creation process.</p>
                                <p>
                                    <strong>+ I created an event using the old design a few weeks ago. Why am I seeing the new design when I go to edit my event? Did all of my settings get transferred over?</strong></p>
                                <p style="padding-left: 30px;">
                                    When you switch to our new design, it changes the setting on all events you create (and have created) in your account. You can switch back to the old design at any point--see above to learn how. All of the data from your event settings have been transferred from the old design to the new design.</p>
                                <p>
                                    <strong>+ Did you make any changes to Eventbrite's fees or pricing?</strong></p>
                                <p style="padding-left: 30px;">
                                    Nope! There were no changes to our pricing or fees. This project was focused solely on taking customer feedback to improve the experience of creating and editing your events.</p>
                                <p>
                                    <strong>+ Where do I set my payment options settings in the new design? I used to enter this info in Step 2: Add Ticket Information in the old design.</strong></p>
                                <p style="padding-left: 30px;">
                                    The <strong>Payment Options</strong> section has moved to the features menu on the left side of your event's <strong>Manage</strong> page. You will be able to set your payment options once you've made your event live. If you are a returning user, we will carry over your previous event settings for payment options.</p>
                                <p>
                                    <strong>+ What are the main changes in the new design?</strong></p>
                                <p style="padding-left: 30px;">
                                    - <span style="text-decoration: underline;">Payment options</span>: This section has moved to the features menu on the left side of your event's <strong>Manage</strong> page. You will be able to set your payment options once you have made your event live. If you are a returning user, we will carry over your previous event settings for payment options.</p>
                                <p style="padding-left: 30px;">
                                    - <span style="text-decoration: underline;">Google Maps</span>: We've added a new plug-in to find your location. Simply enter the name of your venue (optional) and your address and select the address you're looking for from the dropdown menu. If Google can’t find your location, you can always manually enter it by clicking on the <strong>Can’t find your location?</strong> link.</p>
                                <p style="padding-left: 30px;">
                                    - <span style="text-decoration: underline;">Displaying attendees on your event page</span>: You can find this option on the <strong>Colors &amp; Options</strong> page.</p>
                                <p style="padding-left: 30px;">
                                    - <span style="text-decoration: underline;">Global service fee</span>: This option allows you to easily handle service fees in the same manner for all ticket types in your event. If you’d like to offer a different fee structure on a per-ticket-type basis, click on the <strong>Settings</strong> link for each ticket type.</p>
                                <p>
                                    <strong>+ Where are my previously saved locations?</strong></p>
                                <p style="padding-left: 30px;">
                                    Your previously saved locations are available by clicking on <strong>Can't find your location?</strong> in <strong>Step 1: Add your Event Details</strong>, then clicking on the dropdown menu.</p>
                                <p>
                                    <strong>+ How can I offer feedback about the new design?</strong></p>
                                <p style="padding-left: 30px;">
                                    We'd love to get your feedback! Feel free to <a href="http://www.eventbrite.com/contact-us">contact us</a> with any questions or comments about our new design.</p>
