                        <!--step-->
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>To transfer an attendee from an event that uses <a href="/en_us/t/credit-card-processing-faq">credit card processing</a>, you will instead need to <a href="/en_us/t/refund-an-order">refund</a> the order and request that the attendee buy a ticket for the other event.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    You'll need to first mark the original order as canceled by following the instructions below. (This will <strong>not</strong> refund the order in your PayPal or Google Checkout account.)</p>
                                <p>
                                    To get started, go to the <strong>Manage</strong> page of the event for which the original order was placed. You can get there by clicking on <strong>My Events</strong> at the top of the page, and then clicking on the name of your event.</p>
                                <p>
                                    Once on the <strong>Manage </strong>page, select <strong>Event Reports</strong> on the features menu on the left side of the page.</p>
                                <p>
                                    <img border="0" src="http://eventbrite-s3.s3.amazonaws.com/static/images/tutorials/transfer-attendee-from-one-event-to-another/features.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    From the <strong>Report Type </strong>dropdown menu, select <strong>Order Details</strong>.</p>
                                <p>
                                    <img width="621" height="188" border="0" src="http://eventbrite-s3.s3.amazonaws.com/static/images/tutorials/transfer-attendee-from-one-event-to-another/order%20details.png" alt=""></p>
                                <p>
                                    Find the order in question by searching for the attendee's name, email address, or order number.&nbsp;</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Select <strong>Edit Tickets/Amount</strong> from the bottom <strong>Quick Actions</strong> dropdown menu, next to the name of the attendee.</p>
                                <p>
                                    <img width="620" height="461" border="0" src="http://eventbrite-s3.s3.amazonaws.com/static/images/tutorials/transfer-attendee-from-one-event-to-another/edit_tickets_amount.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    Reduce the <strong>Quantity</strong> of the ticket type(s) that you'd like to transfer by the number of that ticket type that you want to transfer to the other event. You'll see the amount in the <strong>Paid</strong> column(s) reduce by the corresponding amount(s).</p>
                                <p>
                                    Select <strong>Yes, update the order total to reflect my changes</strong>, include any notes you'd like for internal purposes, and click <strong>Save Changes</strong>.</p>
                                <p>
                                    <img width="619" height="543" border="0" src="http://eventbrite-s3.s3.amazonaws.com/static/images/tutorials/transfer-attendee-from-one-event-to-another/quantity.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>This will <strong>not</strong> affect the transaction in your PayPal or Google Checkout account. In order to refund the order, you would need to log in to your PayPal or Google Checkout account and manually process the refund there.&nbsp;</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    After you've marked the original order as canceled by following the instructions above, now you'll need to add the attendee to your other event. Learn how to manually add an attendee to an event by going <a href="/en_us/t/add-attendees-manually">here</a>.&nbsp;</p>
                            </div>
