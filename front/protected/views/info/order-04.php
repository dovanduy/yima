                                <p>
                                    <strong>Note: </strong>If you're an attendee who has misplaced the original order confirmation email and/or your PDF tickets, or are wishing for a receipt, you'll need to contact the organizer of the event. Learn how to do that <a href="/en_us/t/i-have-a-question-about-an-event">here</a>.&nbsp;</p>
                            </div>
