                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    For organizers wanting to resend an order confirmation email, first go to the <strong>My Events</strong> page. Then click on the title of your event to go to the <strong>Manage </strong>page for that event. On the <strong>Manage </strong>page, select <strong>Event Reports</strong> from the features menu on the left side of the page.</p>
                                <p>
                                    <img width="231" height="199" border="0" src="http://eventbrite-s3.s3.amazonaws.com/static/images/tutorials/resend_conf_email/event%20reports22.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Filter the <strong>Report Type </strong>dropdown menu to <strong>Order Details </strong>and locate the order in the report.&nbsp;</p>
                                <p>
                                    <strong><img width="481" height="107" border="0" src="http://eventbrite-s3.s3.amazonaws.com/static/images/tutorials/resend_conf_email/order%20report%20selected.png" alt=""></strong></p>
                                <p>
                                    From the <strong>Quick Actions</strong> dropdown menu located on the right side of the order header, select <strong>Resend Confirmation Email.</strong></p>
                                <p>
                                    <img width="511" height="116" border="0" src="http://eventbrite-s3.s3.amazonaws.com/static/images/tutorials/resend_conf_email/quick%20actions%20menu.png.png" alt="How to resend the order confirmation email"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    On the next page, verify the recipient's email address. (You can also enter in a different address to which to resend the order confirmation email.) Click <strong>Send</strong>.</p>
                            </div>
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>If you do enter in a different email address to which to send the order confirmation email, doing so will <em>not</em> permanently change the email address associated with that order.</p>
