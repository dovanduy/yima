                            <div class="stepcontent">
                                <p>
                                    <strong>How do I pay my invoice?</strong></p>
                                <p>
                                    When you log in to your account, click the <strong>Pay Now</strong> button at the top of the <strong>My Events</strong> page.</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 525px; height: 187px; " src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/understanding-your-invoice/invoice31.png" alt=""></p>
                                <p>
                                    You can also click on the blue tab with your email address in the upper right, select <strong>Account</strong> from the dropdown menu, and then select&nbsp;<strong>Invoices</strong>&nbsp;from the menu on the left to view any outstanding invoices (as well as past invoices and current fees to be invoiced).</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 525px; height: 207px; " src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/understanding-your-invoice/invoice32.png" alt=""></p>
                            </div>
                        </div>		
