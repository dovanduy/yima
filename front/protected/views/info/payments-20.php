                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>The admin email address--as well as any <a href="/en_us/t/multiuser-account-access">multi-users</a> on the account who have been granted permission to receive invoice emails--will receive an email notification when an invoice has been issued. Invoices are generated on the first of each month for all ticket sales during the prior month.</p>
                            </div>
