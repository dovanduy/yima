                                <p>
                                    <strong>What methods of payment can I use to pay my Eventbrite invoice?</strong></p>
                                <p>
                                    You can pay your invoice with a credit/debit card in PayPal or using your PayPal account. For U.S. dollar-denominated events, you can also send in a physical check.&nbsp;</p>
                                <p>
