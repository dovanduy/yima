                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Why am I being invoiced?</strong></p>
                                <p>
                                    If you selected PayPal and/or Google Checkout to process payments, or have orders using an <a href="/en_us/t/offline-payment-options">offline payment</a> method, you have collected both the ticket price and the <a href="http://www.eventbrite.com/fees/">Eventbrite fee</a>. This is true even when you have chosen to have the Eventbrite fee added to the ticket price so that your attendees pay the fee.</p>
                                <p>
                                    While PayPal and Google Checkout take out their processing fee--typically 2.9% + $0.30--payments do not flow through Eventbrite for events using these types of payment processing. That means that we don't have the opportunity to collect our fees at the time of purchase, and so send you a monthly invoice.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note:</strong> Want to avoid paying monthly invoices and offer a smoother payment experience for attendees on your next event? Switch to <a href="/en_us/t/credit-card-processing-faq">credit card processing</a>!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>How is my invoice calculated?</strong></p>
                                <p>
                                    To calculate the total shown on your invoice, we multiply the number of tickets sold during the invoice period by:</p>
                                <p>
                                    <a href="http://www.eventbrite.com/fees"><img border="0" title="How much does it cost to use Eventbrite?" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/understanding-your-invoice/fees2.png" alt="How much does it cost to use Eventbrite?"></a></p>
                                <p>
                                    - For example, Laura has one event and sold one ticket for $200. She collected her ticket revenue in addition to the Eventbrite fee on behalf of Eventbrite.</p>
                                <p>
                                    - The Eventbrite fee is 2.5% of the price of each ticket ($5.00) + $0.99 per ticket sold, so the fee on one ticket is $5.99.</p>
                                <p>
                                    - Therefore, her invoice for that month will be $5.99.</p>
                            </div>
                        </div>
