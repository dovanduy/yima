                        <div class="block-heading">
                            <h3>Embed registration form into your website</h3>
                            <ul>
                                <li>
                                    Show your entire event page on your outside website</li>
                                <li>
                                    People initiate their orders while on your site</li>
                                <li>
                                    Auto-generate HTML to drop into your website, Wordpress or Tumblr</li>
                                <li>
                                    Your website must support iframes to embed the registration form</li>
                            </ul>
                        </div>

                        <!--step-->

                        <p>&nbsp;</p>
                        <div class="step">
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> Use this feature to easily add event registration to your own website, content management system (like Drupal and Joomla), or Wordpress or Tumblr blog. Your website must support iframes in order to embed these widgets. If your website was built with Wordpress, for instance, you'd need install a <a href="http://wordpress.org/extend/plugins/search.php?q=embed+iframes&amp;amp;sort=">plugin </a>(website in English)&nbsp;allowing you to embed iframes in your site.</p>
                                <p>
                                    Widgets like the ticket widget cannot be used in conjunction with our <a href="/en_us/t/invitationonly-events">invite-only feature</a>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To get started, first <a href="https://www.eventbrite.com/signup">sign up</a> for a free Eventbrite account or<a href="https://www.eventbrite.com/login"> log in</a> to your existing Eventbrite account and<a href="/en_us/t/event-page-creation"> create an event</a>.</p>
                                <p>
                                    For an already-created event, click <strong>My Events</strong> at the top of the screen and then click on the name of your event to go to that event's <strong>Manage</strong> page.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    On your event's <strong>Manage</strong> page, select <strong>Add Registration to Website</strong>, found in the&nbsp;<strong>Promote</strong> section of the features menu on the left side of the&nbsp;page.</p>
                                <p>
                                    <img alt="" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/use_website_int/reg_page.png" border="0"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img alt="Note" src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note:&nbsp;</strong>The&nbsp;<strong>Promote</strong> section won't appear until you've published your event or made it live..</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    You'll see the preview of the event page that you've created and customized. Scroll down to the HTML code<strong> </strong>for the widget, in the section labelled <strong>Copy and paste this code for use on a webpage</strong>. Click the <strong>Copy to Clipboard </strong>button. Now all you have to do is paste the code into your own website or blog.</p>
                                <img alt="How to use Website Integration" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/use_website_int/reg_page_2.png" border="0" height="260" width="608">
                            </div>
                        </div>
