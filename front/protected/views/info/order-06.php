                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    From the <strong>My Events</strong> page, click on the title of your event to go to the <strong>Manage </strong>page for that event. On the <strong>Manage </strong>page, select <strong>Event Reports</strong> from the features menu on the left side of the page.</p>
                                <img width="216" height="163" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event_reports/eventreporto.png" alt="">
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Set the <strong>Report Type </strong>dropdown menu to <strong>Order Details. </strong>Find the order you would like to delete in the <strong>Search for Orders</strong>&nbsp;field by order number, name or email address.</p>
                                <p>
                                    <strong><img width="605" height="188" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/delete-a-free-order-or-attendee/order_details.png" alt=""></strong></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>To delete or cancel a completed order:</strong></p>
                                <p>
                                    Select <strong>Delete this order</strong> or <strong>Cancel this order</strong> from the top <strong>Quick Actions</strong> menu located on the right side of the gray order box. When asked to confirm the action, click <strong>Yes</strong>.</p>
                                <p>
                                    The only difference between deleting and canceling an order is that an email notification will be sent to the attendee(s) if you choose to cancel the order; no email will be sent if you choose to delete the order.</p>
                                <p>
                                    <img width="609" height="200" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/delete-a-free-order-or-attendee/delete%20order.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>Once an order has been deleted or canceled, the name(s) of the attendee(s) in that order won't show up in the attendee list. To view a deleted or canceled order, select <strong>Refunded and Canceled Orders </strong>from the <strong>Order Type</strong> dropdown menu when viewing an <strong>Order Details </strong>report.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>To delete or cancel just one attendee in an order that includes other attendees:</strong></p>
                                <p>
                                    Find the order in which the attendee appears, per step 2 above.</p>
                                <p>
                                    Select <strong>Delete this Attendee </strong>or<strong> Cancel this Attendee </strong>from the <strong>Quick Actions</strong> menu located to the right of that particular attendee's name. When asked to confirm the action, click <strong>Yes</strong>.</p>
                                <p>
                                    Unlike deleting or canceling an entire order--as detailed in step 3 above--there is no difference between deleting or canceling an individual attendee: the attendee will <em>not</em> receive a notification email.&nbsp;</p>
                                <p>
                                    <img width="607" height="204" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/delete-a-free-order-or-attendee/delete%20attendee.png" alt=""></p>
                            </div>
