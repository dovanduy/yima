                                <p>
                                    <strong>Note: </strong>If you registered for an event and would like to cancel your registration, go <a href="/en_us/t/can-i-get-a-refund">here</a> for more information. If you are an event organizer and would like to issue a complete or partial refund for a <em>paid </em>order, go <a href="/en_us/t/refund-an-order">here</a>.</p>
