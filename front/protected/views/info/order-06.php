                            <h3>Cancel or delete a free order</h3>
                            <ul>

                                <li>
                                    Free up a spot at your event</li>
                                <li>
                                    Canceling an order will send email notification to attendee</li>
                                <li>
                                    Deleting an order will not send an email notification to attendee</li>

                            </ul>
