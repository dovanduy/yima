                        <p>
                            <strong>How to view and/or export to Excel the Attendee Summary report:</strong></p>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> Use the <strong>Attendee Summary</strong> to view your attendees' information and all their answers to your <a href="/en_us/t/collect-information-from-attendees">custom survey</a> and questions.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To access the <strong>Attendee Summary</strong> from the <strong>My Events</strong> page, click on the name of an event&nbsp;or&nbsp;<strong>Manage</strong>&nbsp;under&nbsp;<strong>Quick Links</strong>&nbsp;to go to the&nbsp;<strong>Manage&nbsp;</strong>page for that event. On the <strong>Manage</strong> page, select <strong>Event Reports</strong> from the features menu on the left side of the page.</p>
                                <p>
                                    &nbsp;</p>
                                <p>
                                    <img src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event_reports/eventreporto.png" alt="Screenshot"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Select <strong>Attendee Summary </strong>from the <strong>Report Type</strong> dropdown menu, if it isn't already selected. To sort, filter and save settings for your report, select the <strong>More Options</strong> link located to the right of the <strong>Report Type</strong> dropdown menu.</p>
                                <p>
                                    <img style="width: 573px; height: 209px; " src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event_reports/options.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    You can now view the <strong>Attendee Summary</strong>, which includes the answers to your custom questions that you set up before publishing your event using the <a href="/en_us/t/collect-information-from-attendees"><strong><strong>Customize Order Form</strong></strong></a> feature.</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 525px; height: 368px; " src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event_reports/summary.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    You can edit an attendee's information or order by using the <strong>Quick Actions</strong> dropdown menu, located on the far right of the row containing the attendee's name. (You may need to scroll to the right to see this menu.)</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 235px; height: 189px; " src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event_reports/quick actions.png" alt="Screenshot"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    To export the <strong>Attendee Summary</strong> to Excel or as a .csv or text file, click the, click the&nbsp;<strong>File Type&nbsp;</strong>button to the right of&nbsp;<strong>Export</strong>.</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 358px; height: 57px; " src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event_reports/export.png" alt="Screenshot"></p>
