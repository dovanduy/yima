                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Event calendar widget:</strong> To display a calendar widget showing all your upcoming events, select <strong>Display Event Calendar on Site</strong>.</p>
                                <p>
                                    <img border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/ticket_widget/calendar%20manage.png" alt=""></p>
                                <p>
                                    Choose from a variety of customization options, including selecting a color scheme to match that of your website. You can choose to display events by all organizers in your account, or just the organizer of the event you're currently accessing. You can also choose to display both public and private events, or only public events.</p>
