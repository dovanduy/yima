                                    Visitors to your website can now initiate a ticket sale right from your site!</p>
                                <p>
                                    <img width="611" height="315" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/ticket_widget/ticket_form3.png" alt=""></p>
