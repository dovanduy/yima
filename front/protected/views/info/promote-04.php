                                    You'll see a preview of the registration page at the top. Scroll down, then copy and paste the ready-made HTML code into your own website.</p>
                                <p>
                                    Visitors to your website can now see all your event details and initiate a ticket sale right from your site!</p>
                                <p>
                                    <img width="601" height="539" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/ticket_widget/event%20registration.png" alt=""></p>
