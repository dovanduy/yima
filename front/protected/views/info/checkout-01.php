                            <p>
                                <strong>How to collect custom information from your attendees:</strong></p>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    From the <strong>My Events</strong> page, click on the name of an event to go to the <strong>Manage </strong>page for that event. On the <strong>Manage</strong> page, select <strong>Customize Order Form</strong> from the features menu on the left side of the page.</p>
                                <p>
                                    <img width="228" height="172" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_survey/customizeorderold.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Select 1 of 3 information-collection settings:</p>
                                <p>
                                    1. Collect just the <strong>name &amp; email</strong> of the person buying the ticket(s) or registering for the event (<strong>basic information</strong>).</p>
                                <p>
                                    2. Collect information from just the <strong>ticket buyer</strong> (one survey per order).</p>
                                <p>
                                    3. Collect information from <strong>each attendee</strong> (one survey per ticket).</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note:</strong> If you select option 1 or 2, the same name will appear on all tickets in a multiple-ticket order.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Choose from the default list of questions to ask by checking the <strong>Included </strong>box. You can choose to make a question required by checking the <strong>Required</strong> box.</p>
                                <p>
                                    <img width="611" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_survey/customform2.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    You can create your own custom survey questions by selecting <strong>Create a Question</strong> under <strong>Your Questions</strong> (optional). <a href="/en_us/t/custom-questions-for-attendees">Learn More</a></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    There are a number of <strong>Optional Settings</strong> at the bottom that include:</p>
                                <p>
                                    - Title of, and instructions concerning, the page where you will be collecting information from your attendees;</p>
                                <p>
                                    - The time limit to complete an order before the tickets in that order revert from a "pending" state and again become available to other attendees (the default is 15 minutes);</p>
                                <p>
                                    - The message displayed on your event page after sales have ended;</p>
                                <p>
                                    - The option to allow attendees to update their information after they have registered by logging in to Eventbrite;</p>
                                <p>
                                    - Allowing attendees to request a refund.</p>
                                <p>
                                    <img width="611" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_survey/optionalsettingssurvey.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                6</div>
                            <div class="stepcontent">
                                <p>
                                    Click <strong>Save Changes</strong> to activate the questions/changes you've made for the attendee registration process, as outlined above.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
