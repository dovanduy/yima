                        <div class="block-heading">
                            <h3>"Event capacity" explained</h3>
                            <ul>
                                <li>
                                    Cap the number of tickets available across all ticket types</li>
                                <li>
                                    Guarantee you don't oversell your venue</li>
                            </ul>
                        </div>

                        <div class="alert alert-block">
                            Are you using our new design to create or edit your events?
                            <a href="#newcreate"> Jump to new help </a>
                            <span>
                                or <a href="#"> Learn more </a>
                            </span>
                        </div>

                        <!--step-->

                        <div class="step">
                            <div class="stepcontent">
                                <strong>Tip:</strong> The <strong>Event Capacity</strong> tool sets the maximum number of tickets--across <strong>all </strong>of your ticket types--that can be sold for your event. For example, this feature lets you create two or more ticket types--each with its own <strong>Quantity Available</strong>--and allows you to cap the total number of tickets sold for the event across all ticket types.</div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To get started, first <a href="https://www.eventbrite.com/signup">sign up</a> for a free Eventbrite account or<a href="https://www.eventbrite.com/login"> log in</a> to your existing Eventbrite account and<a href="/en_us/t/event-page-creation"> create an event</a>.</p>
                                <p>
                                    If you’d like to view or edit the <strong>Event Capacity</strong> for an already-created event--after having created at least two <a href="/en_us/t/ticket-type-creation">ticket types</a> for that event--click <strong>My Events</strong> at the top of the screen and then click on <strong>Edit </strong>to the right of your event’s name.</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 595px; height: 267px;" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/managing-your-event-capacity/edit.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    On your event's <strong>Edit</strong> page, go to <strong>Step 2: Add Ticket Information</strong>.</p>
                                <p>
                                    Find the <strong>Event Capacity</strong> field. This field is blank by default, which will allow you to sell up to the <strong>Quantity Available</strong> that you have set for each of your ticket types. (To edit the <strong>Quantity Available</strong> for a ticket type, click on the name of that ticket type to open the <strong>Edit Ticket</strong> window.)</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 595px; height: 226px;" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/managing-your-event-capacity/capacity.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img style="border-width: 0px; border-style: solid;" src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>If your event has only one ticket type, there's no need to set an <strong>Event Capacity</strong> for your event. Your event page will display as&nbsp;<strong>Sold Out</strong> once ticket sales reach the <strong>Quantity Available </strong>for that ticket type, which is set and can be edited as described in step 2 above.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    If your event has two or more ticket types, you can set an <strong>Event Capacity</strong> for your event, if you'd like. This will set the overall maximum number of tickets that can be sold for the event.</p>
                                <p>
                                    As an example, let's say you have a venue with a total capacity of 100 people. You're selling two ticket types--General Admission and VIP--and don't care about the combination of ticket types that you sell, as long as you don't sell more than 100 total tickets. In this case, you'd want to set the <strong>Quantity Available</strong> for each ticket type--as described in step 2 above--to 100, and then also set the <strong>Event Capacity</strong> to 100.</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 595px; height: 248px;" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/managing-your-event-capacity/example.png" alt=""></p>
                            </div>
