                            <h3>Fee-handling options</h3>
                            <ul>
                                <li>
                                    Attendees pay fees by default (fees added to price)</li>
                                <li>
                                    Choose to pay fees yourself instead (fees included in price)</li>
                            </ul>
                        </div>

                        <div class="alert alert-block">
                            Are you using our new design to create or edit your events?
                            <a href="#newcreate"> Jump to new help </a>
                            <span>
                                or <a href="#"> Learn more </a>
                            </span>
                        </div>

                        <!--step-->
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> This tutorial covers how to select who&mdash;you, the organizer, or your attendees&mdash;pays the <a href="http://www.eventbrite.com/fees/">fees</a> for your paid event. By default, the fees are added to the ticket price so that your attendees pay all fees, though you can change this, as detailed below.</p>
                                <p>
                                    Remember, though, that Eventbrite is free to use if your event free!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    From the <strong>My Events</strong> page, click on <strong>Edit </strong>next to the name of an event to go to that event's <strong>Edit </strong>page. (If you are setting up a new event page--that is, you just pressed <strong>Create Event</strong> and the page you are on says <strong>Create an Event</strong>--you can skip this step and go directly to the second part of step 2 below.)</p>
                                <p>
                                    <img width="617" height="342" border="0" title="How to create ticket types" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/fee-handling-options/edit.png" alt="How to create ticket types"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    In <strong>Step 2: Add Ticket Information</strong>, click on the name of the ticket type (in blue) to bring up the <strong>Edit Ticket</strong> window for that ticket type.</p>
                                <p>
                                    <img width="617" height="222" border="0" title="How to create ticket types" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/fee-handling-options/step%202.png" alt="How to create ticket types"></p>
                                <p>
                                    If you are setting up a new event page, and so are on the <strong>Create an Event </strong>page, click <strong>Create a Ticket</strong>, fill out the name, price and quantity available of that ticket type, and then continue to step 3 below.</p>
                                <p>
                                    <img border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/fee-handling-options/create%20ticket.png" alt=""></p>
                                <p>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Near the bottom of the <strong>Edit Ticket</strong> window, click <strong>Show</strong> next to <strong>Advanced Options</strong> to display more options. You will find the <strong>Service Fee</strong> options near the bottom of the expanded window. (If you have previously viewed the Advanced Options, you will not need to click <strong>Show</strong>, as the expanded window will automatically display.)</p>
                                <p>
                                    <img width="616" height="310" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/fee-handling-options/edit%20ticket.png" alt=""></p>
                                <p>
                                    <img width="615" height="684" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/fee-handling-options/advanced%20options.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    There are three options from which you can choose regarding who pays the fees for your event. They are as follows:</p>
                                <p>
                                    <strong>ADD fees on top of total ticket price</strong></p>
                                <p>
                                    -This means that your attendees will pay all fees.</p>
                                <p>
                                    -You, the organizer, will receive the entire price of each ticket, as the attendees will pay all fees.</p>
                                <p>
                                    -This is the default option.</p>
                                <p>
                                    -Here is how your event page would display for a $20 ticket with this option selected:</p>
                                <p>
                                    <img width="617" height="124" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/fee-handling-options/add%20fees.png" alt=""></p>
                                <p>
                                    <strong>INCLUDE fees into total ticket price</strong></p>
                                <p>
                                    -This means that you will pay all fees.</p>
                                <p>
                                    -You, the organizer, will receive the price of each ticket, minus the fees.</p>
                                <p>
                                    -This is the recommended option for <a href="/en_us/t/collect-donations">donation</a> ticket types.</p>
                                <p>
                                    -Here is how your event page would display for a $20 ticket with this option selected:</p>
                                <p>
                                    <img width="616" height="127" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/fee-handling-options/include%20fees.png" alt=""></p>
                                <p>
                                    <strong>INCLUDE credit card processing fee in the total ticket price and ADD the Eventbrite fee on top of total ticket price</strong></p>
                                <p>
                                    -This is an option only if you choose credit card processing for your event.</p>
                                <p>
                                    -This means that you will pay the credit card processing fee, and your attendees will pay the Eventbrite fee.</p>
                                <p>
                                    -Here is how your event page would display for a $20 ticket with this option selected:</p>
                                <p>
                                    <img width="614" height="123" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/fee-handling-options/split%20fees.png" alt=""></p>
                            </div>
                            <p>
                                &nbsp;</p>
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>If you have selected PayPal or Google Checkout to process your credit card payments, you cannot pass along to your attendees the processing fee that they charge, unfortunately. You can, however, choose for your attendees to pay the Eventbrite fee, by choosing to <strong>ADD fees into total ticket price</strong>, as detailed above.&nbsp;</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    Once you make your selection, be sure to click <strong>Save</strong> to save your changes and to go to the <strong>Edit</strong> page for your event.</p>
                                <p>
                                    To see how the fees will display to your attendees, click <strong>View </strong>(or <strong>Preview</strong> if you haven't yet published your event) in the upper left of the <strong>Edit</strong> page to check out your event page in a new browser tab/window.</p>
                                <p>
                                    <img width="617" height="407" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/fee-handling-options/view.png" alt="How to collect donations on the event page"></p>
                            </div>
                        </div>
