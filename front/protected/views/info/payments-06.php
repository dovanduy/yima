                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> This tutorial covers how to select who&mdash;you, the organizer, or your attendees&mdash;pays the <a href="http://www.eventbrite.com/fees/">fees</a> for your paid event. By default, the fees are added to the ticket price so that your attendees pay all fees, though you can change this, as detailed below. Remember, though, that Eventbrite is free to use if your event is free!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To get started, first <a href="https://www.eventbrite.com/signup">sign up</a> for a free Eventbrite account or <a href="https://www.eventbrite.com/login">log in</a> to your existing Eventbrite account and <a href="/en_us/t/event-page-creation">create an event</a>.</p>
                                <p>
                                    If you’d like to edit an already-created event, click <strong>My Events</strong> at the top of the screen and then, click on <strong>Edit </strong>to the right of your event’s name.</p>
                                <p>
                                    <img width="608" height="220" border="0" title="How to create ticket types" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/fee-handling-options/feehandle_1b.png" alt="How to create ticket types"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Go to <strong>Step 2:</strong> <strong>Create Tickets</strong>. Use the <strong>Global Service Fee </strong>tool to make the same selection for all types of tickets for this event.</p>
                                <p>
                                    <img width="609" height="251" border="0" title="How to create ticket types" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/fee-handling-options/feehandle_2b.png" alt="How to create ticket types"></p>
                                <p>
                                    If you would like to choose who pays the fees on a per-ticket basis, select <strong>Customize per ticket type</strong> and/or click on the <strong>Settings</strong> for a ticket type and make your selection in the <strong>Service Fees</strong> section.</p>
                                <p>
                                    <img width="610" height="310" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/fee-handling-options/feehandle_3.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note:</strong> Click <a href="http://www.eventbrite.com/fees/">here</a> to see more information about Eventbrite's fees.&nbsp;</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    By default, <strong>Pass on fees to the buyer</strong> will be selected, meaning that the fees will be added on to the ticket price and paid by the buyer. You, the organizer, will receive the entire price of each ticket.</p>
                                <p>
                                    Here’s how your event page would display for a $20 ticket with this option selected:</p>
                                <p>
                                    <img width="516" height="254" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/fee-handling-options/feehandle_4.png" alt=""></p>
                                <p>
                                    If you choose <strong>Absorb the fees</strong>, the buyer will pay just the price of the ticket and the fees will be deducted from that price. You, the organizer, will receive the price of each ticket, minus the fees. This is the recommended option for <a href="/en_us/t/collect-donations">donation</a> ticket types.</p>
                                <p>
                                    Here’s how your event page would display for a $20 ticket with this option selected:</p>
                                <p>
                                    <img width="521" height="245" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/fee-handling-options/fees3b.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note:&nbsp;</strong> If you have <a href="/en_us/t/payment-processor-options">selected</a> PayPal or Google Checkout to process your credit card payments, you unfortunately cannot pass the processing fee that PayPal or Google Checkout charges along to your attendees. You can, however, choose for your attendees to pay the Eventbrite fee, by choosing to <strong>Pass on fees to the buyer</strong>, as detailed above.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    Once you make your selection, be sure to click <strong>Save</strong> to save your changes.</p>
                                <p>
                                    To see how the fees will display to your attendees, click <strong>Colors &amp; Options</strong> in the upper left to take a look at your event page!</p>
