                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <b>Note: </b>If an email address is already associated with an Eventbrite account, you will not be able to add that email as a subuser. Why? When adding a subuser to your account, you are also creating an account for them, and an email address cannot be associated with two Eventbrite accounts. To bypass the following steps, just add an email address--the potential subuser's personal email address, for instance--that is not already registered with Eventbrite.</p>
                                <p>
                                    If you're trying to make the current subuser on an account the administrator on that account, click <a href="/en_us/t/making-a-subuser-the-account-admin/">here</a> for instructions.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    If the subuser's email is already registered with an Eventbrite account, you will see the following error.</p>
                                <a href="http://eventbrite.com/resetpassword"><img width="585" height="392" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/multi-user-already-registered/subuserhelp1.png"></a>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    The subuser that you are unable to add needs to log in to their Eventbrite account and close their account. Have them click on the<b> Account</b> tab located at the top right side of the page.</p>
                                <img width="583" height="61" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/multi-user-already-registered/subuserhelp2.png" alt="How to add multi-users to your account">
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <b>Tip:</b> If the subuser is having trouble remembering their password to log in to their account, they can reset their password by clicking <a href="http://eventbrite.com/resetpassword">here</a>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    From the <b>Account </b>tab, the subuser needs to click on <b>Close Account</b>. They will be asked to provide a reason for why they are closing their account (they can say something like 'switching email addresses on account'), type in CLOSE, and their password. Lastly, they need to click on the green button that says <b>Close Account</b>.</p>
                                <img width="610" height="383" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/multi-user-already-registered/subuserhelp33.png">
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    Their Eventbrite account is now closed and their email address is free for use!&nbsp;</p>
                                <img border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/multi-user-already-registered/subuserhelp4.png">
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    You're almost there! Log in to your account--i.e., the admin account--and click on the<b> Account</b> tab located at the top right side of the page. Then, select <b>Multi-User Access </b>on the left hand side.</p>
                                <div class="step">
                                    <img border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/multi-user-already-registered/subuserhelp6.png">
                                </div>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
