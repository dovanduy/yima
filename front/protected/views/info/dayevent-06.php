                        <div class="block-heading">
                            <h3>Laptop for check-in</h3>
                            <ul>
                                <li>
                                    Use one or more laptops to check in attendees at event</li>
                                <li>
                                    Internet connection needed</li>
                                <li>
                                    Works with our Entry Manager <a href="/en_us/t/iphone-for-checkin">iPhone</a> and <a href="/en_us/t/android-for-checkin">Android</a> apps</li>
                                <li>
                                    Go <a href="/en_us/t/disable-paper-tickets">paperless</a>!</li>
                            </ul>
                        </div>

                        <!--step-->
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To access the Entry Management tool from within your account, click on the title of your event on the <strong>My Events </strong>page to go to the <strong>Manage </strong>page for that event. On the <strong>Manage </strong>page, select <strong>Check In Attendees</strong> from the features menu on the left side of the page.</p>
                                <p>
                                    <img width="233" height="137" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/entry-management/sidebar1.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>Multi-user (subuser) access to Entry Management is set in the admin account by clicking <strong>Account</strong> in the upper right, then clicking <strong>Multi-User Access</strong>. Then click <strong>Edit</strong> next to a subuser's email address, and, in the <strong>Actions this user can perform </strong>section, check the box next to <strong>Check in attendees</strong>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    There are columns for check-in <strong>Status</strong>, <strong>Attendee Name </strong>(sorted by first name), <strong>Email</strong>, and <strong>Ticket Type</strong> that you can sort by clicking on the name of each column. The status bar at the top shows the number and percentage of attendees that are currently checked in.</p>
                                <p>
                                    You can also search for an attendee by their name or email address. The attendee list will automatically filter as you type in the search box.</p>
                                <p>
                                    <strong><img width="605" height="526" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/entry-management/interface.png" alt=""></strong></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Click the green <strong>Check In </strong>button to check in an attendee. The button will then change to show <strong>Checked In</strong>.</p>
                                <p>
                                    <img width="614" height="83" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/entry-management/checked%20in.png" alt=""></p>
                                <p>
                                    For events in which you are collecting basic information or information from the ticket buyer only, a dropdown menu showing the number of tickets checked in out of the total number of tickets for the order will display for multiple-ticket orders.</p>
                                <p>
                                    (Click <a href="/en_us/t/collect-information-from-attendees">here</a> to learn more about the different options for collecting information from your attendees.)</p>
                                <p>
                                    <img width="612" height="82" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/entry-management/multiple.png" alt="How to refund an order or attendee"></p>
                                <p>
                                    <strong>Checked In</strong> will show when all tickets in a multiple-ticket order have been checked in.</p>
                                <p>
                                    <img width="619" height="82" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/entry-management/2%20of%202.png" alt="How to refund an order or attendee"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    To check out an attendee that has already checked in, mouse over <strong>Checked In</strong> and click the gray <strong>Check Out</strong> that appears. A green <strong>Check In </strong>button will then show again, indicating that this attendee has been checked out.</p>
                                <p>
                                    <img width="621" height="82" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/entry-management/check%20out.png" alt=""></p>
                                <p>
                                    For multiple-ticket orders in events in which you are collecting basic information or information from the ticket buyer only, click the gray <strong>Check Out </strong>button to check out one attendee.</p>
                                <p>
                                    You can then adjust the total number of attendees you would like to check out using the dropdown menu.</p>
                                <p>
                                    <img width="620" height="84" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/entry-management/multiple2.png" alt="How to refund an order or attendee"></p>
                                <p>
                                    "0" out of the total number of tickets in this order will again show when all tickets in a multiple-ticket order have been checked out.</p>
                                <p>
                                    <img width="620" height="83" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/entry-management/multiple.png" alt="How to refund an order or attendee"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    After your event, you can easily view detailed check-in information for your event by selecting <strong>Charts </strong>on the left side of the <strong>Manage</strong> page for that event.</p>
                                <p>
                                    <strong><img border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/entry-management/charts.png" alt=""></strong></p>
