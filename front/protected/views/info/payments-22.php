                                    <strong>Note: </strong>If you're in the UK, Ireland, Canada, Australia, New Zealand, France, Spain, Germany or the Netherlands and would like to charge tax, go <a href="/en_us/t/charge-tax-on-tickets-limited-availability">here</a> to learn how.</p>
                                <p>
                                    We plan to gradually widen the availability of our dedicated tax tool--keep an eye on our <a href="http://blog.eventbrite.com/">blog</a> for announcements as we expand our dedicated tax support! In the meantime, see below to learn how to add tax to your ticket price if you are not in the abovementioned countries.</p>
                            </div>
