                        <div class="stepcontent">
                            <p>
                                <strong>As an example, we'll use a $50 ticket, with 10% tax to be added on to the price</strong></p>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    When on the <strong>Create an Event </strong>page for your event, in <strong>Step 2: Add Tickets</strong>, click the <strong>Create a Ticket</strong> button. (If you've already created a ticket, and want to create an additional ticket type, this button will say <strong>Add a New Ticket</strong>.)</p>
                                <p>
                                    <img border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-charge-tax-on-the-ticket-price/create_ticket.png" alt=""></p>
                                <p>
                                    To add tax to an existing ticket for an existing event, go to the event's <strong>Edit </strong>page and click the name of the ticket type to edit the information for that ticket.</p>
                                <p>
                                    <img width="602" height="202" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-charge-tax-on-the-ticket-price/existing_ticket.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    In the <strong>Edit Ticket</strong> window that appears, indicate in the <strong>Ticket Name</strong> that the tax is included in the price. Make sure to include the sum of your ticket price and any taxes in the <strong>Price</strong> field. Then enter the <strong>Quantity Available</strong> of this ticket type.</p>
                                <p>
                                    In our example, the name of the ticket is 'General Admission,' and we've shown that 10% tax is included. (We've included a line break between the name of the ticket and the tax information by adding in the HTML tag &amp;lt;br/&amp;gt; so that this information will appear on separate lines.) We've then entered $55 as the <strong>Price</strong>, as this is the sum of our original $50 ticket price and the additional $5 in taxes.</p>
                                <p>
                                    <img width="598" height="315" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-charge-tax-on-the-ticket-price/edit_ticket2.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Next, click on <strong>Show</strong> to view the <strong>Advanced</strong> <strong>Options</strong>.</p>
                                <p>
                                    <img width="608" height="317" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-charge-tax-on-the-ticket-price/advanced_options2.png" alt=""></p>
                                <p>
                                    In the <strong>Ticket Description</strong> field, include a message reiterating that the tax is included in the ticket price. This message will appear below the name of your ticket type on your event page. Uncheck the <strong>Auto Hide Description</strong> box, so that this message will always be visible. Make sure to click <strong>Save</strong>.</p>
                                <p>
                                    <img width="602" height="433" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-charge-tax-on-the-ticket-price/ticket_description.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img border="0" src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>By default, the service fee is added on top of any value you include in the <strong>Price</strong> field and so is paid by the attendee. To learn more about the option to absorb the fee yourself, go <a href="/en_us/t/feehandling-options">here</a>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    Back on the main <strong>Edit </strong>or <strong>Create an Event</strong> page, click on <strong>View</strong> or <strong>Preview </strong>in the upper left to view your event page and see how the ticket information is displayed to your attendees. Well done!</p>
                                <p>
                                    <img width="603" height="281" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-charge-tax-on-the-ticket-price/event_page.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> We recommend also adding information about taxes into <strong>Step 3: Add Event Details</strong> on your event's <strong>Edit </strong>page. The more messaging the better, we say!</p>
                            </div>
