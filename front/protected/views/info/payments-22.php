                                <p>
                                    In our example, the name of the ticket is 'General Admission,' and we've shown that 10% tax is included. We've then entered $55 as the <strong>Ticket</strong> <strong>Price</strong>, as this is the sum of our original $50 ticket price and the additional $5 in taxes.</p>
