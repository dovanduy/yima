                        <div class="block-heading">
                            <h3>Organizer profile pages</h3>
                            <ul>
                                <li>
                                    List all your public events on one page and drive customers there</li>
                                <li>
                                    Establish credibility and a strong brand presence</li>
                                <li>
                                    Match your brand's color scheme</li>
                                <li>
                                    Include Facebook and/or Twitter feed</li>
                            </ul>
                        </div>

                        <!--step-->
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> Customizing and publicizing your organizer profile page is a great way to drive all your potential attendees to a single page listing all your public events. Be sure to blast out its link via email and your online social networks, post it on your own website, and use it on any flyers or advertising materials! Read more below about all the different features and options you can customize in the <strong>My Profile</strong> tab.</p>
                                <p>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <strong><span style="font-size: large;">The organizer profile page</span></strong>
                        </p>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    Here's an example organizer profile page:</p>
                                <p>
                                    <img width="615" height="568" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/organizer-profile-pages-and-the-my-profile-tab/page.png" alt=""></p>
                            </div>
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    - Notice the social-sharing options at the top of the page, useful so visitors can blast your page to their friends and contacts on Facebook, Twitter and LinkedIn.</p>
                                <p style="text-align: left;">
                                    - A logo and short bio show up at the top of the page.</p>
                                <p style="text-align: left;">
                                    - All your current and past public events are displayed below that, as well as a featured event module. You can also display a clickable link to your outside website, the number of public events you've held, the top categories and primary locations of your public events.</p>
                                <p style="text-align: left;">
                                    - Each event with tickets on sale has a prominent <strong>Attend</strong> button next to it, to drive even more ticket sales your way!</p>
                                <p style="text-align: left;">
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <strong><span style="font-size: large;">Editing your organizer profile page via the My Profile tab</span></strong>
                        </p>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To edit your organizer profile, click on the <strong>My Profile</strong> tab at the top of the screen.</p>
                                <p>
                                    <img width="604" height="374" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/organizer-profile-pages-and-the-my-profile-tab/top.png" alt=""></p>
                                <p>
                                    In the top section of the <strong>My Profile </strong>page, you'll see:</p>
                                <p>
                                    - A <strong>dropdown menu</strong> showing all existing organizers in your account. (You've most likely created these organizers on the <strong>Edit </strong>page of your events, in <strong>Step 6: Add Host</strong>, if you're using our old design, or <strong>Step 1: Add your Event Details</strong>, if you're using our <a href="/en_us/t/more-info-about-our-new-design">new design</a>.) You can edit the profile page for an existing organizer, or create a new organizer and related profile page from this dropdown.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>To delete an unused organizer, first save your changes in <strong>My Profile</strong>. Then click on the <strong>Account </strong>tab in the upper right and click on <strong>Unused Venues &amp; Organizers</strong>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    - An <strong>organizer name</strong> field that will display at the top of the profile page. (If you leave this field blank, "About" will show at the top of the profile page.)</p>
                                <p>
                                    - You can upload an <strong>organizer logo</strong> by clicking <strong>Browse</strong>, selecting an image file from your computer, and then clicking <strong>Upload</strong>, which turns green once you've chosen a valid file. Parameters governing the image file you choose are displaying in this section.</p>
                                <p>
                                    - Throw up a short bio or description in the <strong>about the organizer</strong> section that will display under the organizer name at the top of the profile page. (You can click the <strong>Also use this description for event pages </strong>option to have this description show in the <strong>Hosted by</strong> section on all event pages that have this organizer selected as the host, overriding any description that was previously there.)</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    In the <strong>organizer settings </strong>section you can choose to:&nbsp;</p>
                                <p>
                                    - Display a clickable URL for your <strong>external website</strong>.</p>
                                <p>
                                    - Show the <strong>number of past events</strong> that have been held by this organizer. (Private events are not included in this tally, as only public events are displayed on the organizer profile page.)</p>
                                <p>
                                    - Display your one or two <strong>top event categories</strong>.</p>
                                <p>
                                    - Show your one or two <strong>top event locations</strong>.</p>
                                <p>
                                    - Display only events by this organizer, or by all organizers in your account. (You match an event to an organizer on the <strong>Edit </strong>page for an event, in <strong>Step 6: Add Host</strong>, if you're using our old design, or <strong>Step 1: Add your Event Details</strong>, if you're using our <a href="/en_us/t/more-info-about-our-new-design">new design</a>.)</p>
                                <p>
                                    - Feature a particular event. (By default, the next public event is shown.)</p>
                                <p>
                                    - Customize the organizer profile page URL. Choose a memorable URL, which you can then publicize to your potential attendees! Optimizing your URL is also a great way to rank better in search engines, so choose a URL that utilizes <a href="http://www.google.com/insights/search/">targeted keywords</a>.</p>
                                <p>
                                    <img width="610" height="272" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/organizer-profile-pages-and-the-my-profile-tab/organizer_settings2.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Promote your organizer profile page with an Eventbrite button! You can apply an Evenbrite button to your website or blog so that visitors to the page can click on it to view your organizer profile. First, choose the size of the button that you'd like to display on your webiste. Then click <strong>Copy to Clipboard</strong> and paste the HTML code into your website or blog.&nbsp;&nbsp;</p>
                                <p>
                                    <img width="609" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/organizer-profile-pages-and-the-my-profile-tab/org-profile-button.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    Include your <strong>Facebook Page and/or Twitter feeds</strong> on your profile page to incorporate your wider social media presence, foster greater conversation about your brand, and provide your attendees with the latest info about your events.</p>
                                <p>
                                    - Facebook Page: You can incorporate the feed for your organization's Facebook Page by entering its URL (e.g., http://www.facebook.com/<strong>Eventbrite</strong> or http://www.facebook.com/<strong>pages/Eventbrite/14829466723</strong>).</p>
                                <p>
                                    - Twitter feed: You can enter either a Twitter username to display the feed for that username (e.g., @Eventbrite) or a search query (e.g., #sfevents) to display the real-time feed of search results.</p>
                                <p>
                                    - If you choose to display both a Facebook and a Twitter feed, the Facebook feed will always be displayed by default. Also, due to a limitation with Facebook's API, the default color scheme for the Facebook feed will always show, regardless of whether you customize the color scheme for your profile page (as described below).</p>
                                <p>
                                    <img width="615" height="179" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/organizer-profile-pages-and-the-my-profile-tab/communicate.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    Customize the <strong>organizer profile page colors</strong> using either a preset template--our example above uses the "Sand" palette--or by designing your own custom color scheme. Older users will see an <strong>Advanced </strong>tab that controls whether:</p>
                                <p>
                                    - The selected colors will always be applied to the organizer profile page, or</p>
                                <p>
                                    - The colors of a given event page will be applied to the organizer profile page when an attendee clicks on the "View more events" link in the <strong>Hosted by </strong>section on that event page.</p>
                                <p>
                                    <img width="613" height="196" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/organizer-profile-pages-and-the-my-profile-tab/colors.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                6</div>
                            <div class="stepcontent">
                                <p>
                                    Once you've tricked out the page to your liking, make sure to click <strong>Save</strong>. You can click <strong>View Profile</strong> to take a look at the finished product. Now take the <strong>organizer page URL</strong> and publicize it to the world!</p>
                            </div>
                        </div>
