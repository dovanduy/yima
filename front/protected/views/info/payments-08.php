                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note:&nbsp;</strong>We regret that currently we cannot issue payouts to banks located in Italy or Portugal, though we're actively working to provide this level of support in the future. In the meantime, we suggest&nbsp;<a href="/en_us/t/use-paypal-to-process-orders">PayPal</a> as an alternative to Eventbrite's credit card processing.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>How do I receive my money if I use credit card processing?</strong></p>
                                <p>
                                    After your event, Eventbrite will send you a confirmation email with the details of your event revenue and payout information.</p>
                                <p>
                                    You have 96 hours (four days) from the end of your event to process any <a href="/en_us/t/refund-an-order">refunds </a>and make any adjustments. On the fifth business day after the end date of your event, Eventbrite will mail you a check (for USD events only) or issue a direct deposit for your total event revenue (less Eventbrite and credit card processing fees).</p>
                                <p>
                                    Direct deposits can take up to two business days to appear in your account after the payout is initiated, depending on your bank; checks generally arrive within five business days after the payout is initiated.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>If the fifth day after the end date of your event falls on a weekend or a US banking holiday, the payout will be initiated on the next business day.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>Eventbrite is currently only able to process credit card payouts to banks located in a country that uses the currency in which you priced your tickets on Eventbrite. (For instance, if your tickets are priced in US dollars, you will only be able to receive a USD-denominated payout, sent to a bank located in the United States.) If you wish to receive payout in a different currency or to a bank located in a different country, please select a <a href="/en_us/t/payment-processor-options">different payment processor</a> for your event.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Can I get an example or two of when I might receive my money for a direct deposit?</strong></p>
                                <p>
                                    For example, if your event ends on a Sunday (1/8), your event payout will be issued on the following Friday (1/13), five days after your event ends. If you've chosen to receive a direct deposit, your funds should arrive in your bank account account by the end of Tuesday (1/17), two business days after the payout is initiated.</p>
                                <p>
                                    As another example, if your event ends on a Monday (1/9), your event payout will be issued on the following Monday (1/16). (Since the fifth day after your event ends is a Saturday (1/14), payout is initiated the next business day, which is Monday (1/16).) If you've chosen to receive a direct deposit, your funds should arrive in your bank account by the end of Wednesday (1/18), two business days after the payout is initiated.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Can I change the payment processing method at any time?</strong></p>
                                <p>
                                    After ticket sales have started on an event, you cannot change the payment processing method. However, you can change it at any time during the setup of your event, and you can use a different method for each event you host. Learn more about how to change your payment processing method <a href="t/edit-payment-options">here</a>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>How are refunds processed with credit card processing?</strong></p>
                                <p>
                                    You can refund your guests directly through your Eventbrite account, as long as the payout for your event has not yet been processed and sent by us.</p>
                                <p>
                                    Find step-by-step instructions on how to process refunds <a href="/en_us/t/refund-an-order">here</a>.</p>
                            </div>
