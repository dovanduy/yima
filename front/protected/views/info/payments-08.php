                                    <strong>Note: </strong>For step-by-step instructions on setting up credit card processing for your event, go <a href="/en_us/t/credit-card-processing-setup">here</a>. If your tickets are priced in U.S. dollars, click <a href="/en_us/t/credit-card-processing-faq">here</a> for more information.</p>
