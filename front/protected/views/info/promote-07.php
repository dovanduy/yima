                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> The <strong>Manage Invitations</strong> will not appear in your event's <strong>Manage</strong> page until you have scheduled or sent out email invites. In <strong>Manage Invitations</strong>, you'll see a <strong>status </strong>shown for each of your recipients. Click <a href="/en_us/t/invitation-statuses-explained">here </a>to learn more about what each of these statuses means.&nbsp;</p>
                            </div>
                        </div>

                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To view the recipients of your sent invites, go to your event's <strong>Manage</strong> page by clicking on <strong>My Events</strong> at the top of the page, and then clicking on the name of your event. Once on the <strong>Manage </strong>page, select <strong>Manage Invitations</strong> on the features menu on the left side of the page.</p>
                                <p>
                                    <img border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/track_status_of_invites/manage%20status%281%29.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Each batch of invitations you send will show as a separate line item. You'll see the subject of the email invite, the number of recipients, and the date it was sent. If you click <strong>Copy</strong>, you'll be able to edit your invite before sending it out to more invitees.</p>
                                <p>
                                    If you've sent multiple rounds of invitations, click <strong>View all invited guests </strong>in the lower right to show the <a href="/en_us/t/invitation-statuses-explained">statuses</a> for all recipients of all rounds of invites.</p>
                                <p>
                                    To resend an invite to all recipients or a subset of recipients--e.g., those who haven't responded--first use the <strong>Filter invites</strong> dropdown menu and then click the <strong>Edit and Resend Invites</strong> link. At this point, you'll have the opportunity to edit the invite and recipient list before sending it out.</p>
                                <p>
                                    <img width="609" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/track_status_of_invites/resendinvites.png" alt=""></p>
