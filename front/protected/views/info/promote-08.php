                            <h3>Invitation statuses explained</h3>
                            <ul>
                                <li>
                                    Definitions of all email statuses and RSVP labels</li>
                            </ul>
                        </div>

                        <!--step-->
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <b>Tip:</b> 'Complaint' means that an invitee has marked the email invitation to your event as spam. Make sure to remove this attendee from your address book and/or contact lists before sending out any more email invitations to any of your events!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To learn about how to access <b>Manage Invitations</b>, check out our tutorial <a href="/en_us/t/resend-and-manage-sent-invitations">here</a>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Once on the <b>Manage Invitations</b> page, you will see one of a number of statuses next to each of the email addresses to which you have sent an invitation.</p>
                                <p>
                                    <img width="607" height="513" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/status-of-invitations_definitions/managestatus202.png"></p>
                                <p>
                                    Here are the definitions to help decode each status:</p>
                                <p>
                                    <b>Opened</b>: The recipient opened, loaded the images in the invitation email, and viewed your invitation. Note that the invitation will continue to show as <b>Sent</b> if the recipient has not loaded the images in the email invitation. Email-tracking technology requires that images be loaded in an email in order to activate the tracking pixel that communicates with our mail servers that the email has been opened.</p>
                                <p>
                                    <b>Clicked</b>: Your guest clicked on the invitation and viewed your event page.</p>
                                <p>
                                    <b>Bounced</b>: The invite wasn't delivered, as the email address was invalid. Eventbrite won't send invites for your events to this email address again.</p>
                                <p>
                                    <b>Unsubscribed</b>: The recipient unsubscribed from receiving your invitations. Eventbrite won't send invites for your events to this email address again.</p>
                                <p>
                                    <b>Complaint</b>: The recipient has marked the invite as spam. <b>Be sure to remove any of these email addresses marked as 'Complaint' from your address book and/or <a href="/en_us/t/import-or-create-contact-lists">contact list</a> before sending out any more invitations to any of your events!</b></p>
                                <p>
                                    <b>Undelivered/Blocked</b>: The attempt to deliver the email invite was unsuccessful. However, Eventbrite will continue to send email invites for your events to this email address.</p>
                            </div>
                        </div>
