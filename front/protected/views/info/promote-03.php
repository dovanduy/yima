                        <div class="block-heading">
                            <h3>Import or create contact lists</h3>
                            <ul>
                                <li>
                                    Efficiently manage your contacts</li>
                                <li>
                                    Import contacts from outside databases and/or email services</li>
                                <li>
                                    Create contact lists from attendees of past event</li>
                                <li>
                                    Limit contact lists to fewer than 2000 contacts</li>
                            </ul>
                        </div>

                        <!--step-->
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip: </strong>For more information about sending email invitations, click <a href="/en_us/t/email-invitations">here</a>.</p>
                                <p>
                                    We recommend that you do <strong>not</strong> create any contact list with over 2000 contacts.&nbsp; Eventbrite has a limit of 2000 email invitations that can be sent per account per day, and sending email invitations to a contact list of more than 2000 email addresses can lead to irregularities in the delivery of the email invitations. If you have more than 2000 contacts, feel free to create another contact list!</p>
                            </div>
                        </div>
                        <div class="step">
                            <p>
                                <strong>Here's a handy tutorial video. You can also find written instructions below the video.</strong></p>
                            <object width="555" height="385" type="application/x-shockwave-flash" data="http://www.youtube.com/v/GHem8ClMcqk?fs=1&amp;amp;hl=en_US"><param value="true" name="allowFullScreen"> <param value="always" name="allowscriptaccess"> <param value="http://www.youtube.com/v/GHem8ClMcqk?fs=1&amp;amp;hl=en_US" name="src"> <param value="true" name="allowfullscreen"></object>
                            <p>
                                &nbsp;</p>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    Click on the <strong>My Contacts</strong> tab located at the top of any page.</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 609px; height: 45px; " src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/importing_contacts/my_contacts2.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    To create a new contact list, click <strong>Create a Contact List</strong>.</p>
                                <img width="481" height="224" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/importing_contacts/create_button.png" alt="How to create and save contact lists"></div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Enter the <strong>name of your new contact list</strong> and then choose from one of several <strong>import options</strong> to add contacts to your new list. See below for more details about these options.</p>
                                <p>
                                    <strong>Option 1:</strong> Import contacts from a text .txt or .csv file by uploading that file to Eventbrite.</p>
                                <p>
                                    <img width="421" height="245" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/importing_contacts/new_list.png" alt=""></p>
                                <div class="stepcontent">
                                    <p>
                                        If your contacts are in Excel format, you can convert that file in to .csv or .txt. Make sure your email addresses are in a column with the header <strong>email</strong>.</p>
                                    <p>
                                        OPTIONAL: <strong>Eventbrite</strong><strong> </strong>also imports the first and last names of your attendees on the list, if you have this information. You <strong>must </strong>include the headers <strong>firstname </strong>(no spaces)<strong> </strong>and <strong>lastname</strong> (no spaces) for the first name and last name columns, respectively.</p>
                                    <p>
                                        The <strong>email</strong>, <strong>firstname </strong>(no spaces), and <strong>lastname</strong> (no spaces) columns can be in any order. Any extra columns ('age,' for example) will be ignored. For text (.txt) files, a tab <strong>must</strong> be added after each column header and row entry--that is, press the <strong>Tab</strong> key on your keyboard after each column header and each row entry--even after the last column header and row entry.</p>
                                    <p>
                                        Here is an example of a text (.txt) file with <strong>email</strong> header:</p>
                                    <img border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/importing_contacts/txt1.png" alt="">
                                    <p>
                                        Here is an example of a text (.txt.) file with <strong>email</strong>, <strong>firstname</strong>,<strong> </strong>and <strong>lastname </strong>headers:</p>
                                    <img width="359" height="78" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/importing_contacts/txt2.png" alt="">
                                    <p>
                                        For .csv files, it is recommended to save as <strong>CSV (MS-DOS)</strong>.</p>
                                    <p>
                                        Here's an example of a .csv file with <strong>email</strong> header:</p>
                                    <img width="199" height="116" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/importing_contacts/csv1.png" alt="">
                                    <p>
                                        Here's an example of a .csv file with <strong>email</strong>, <strong>firstname</strong>, and <strong>lastname</strong> headers:</p>
                                    <img width="328" height="116" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/importing_contacts/csv2.png" alt=""></div>
                            </div>
                            <div class="step">
                                <div class="stepnumber">
                                    <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note"></div>
                                <div class="stepcontent">
                                    <p>
                                        <strong>Note: </strong>When importing a contact list, whether in .txt or .csv form, your list <strong>must</strong> contain <strong>email</strong>, <strong>firstname</strong> and <strong>lastname</strong> column headers, regardless of whether your list includes first and/or last names.</p>
                                </div>
                            </div>
                            <div class="step">
                                <div class="stepcontent">
                                    <p>
                                        <strong>Option 2:</strong> Import contacts from your email address book by entering your email or user name and password and clicking <strong>Continue</strong>. You will have the option to include all contacts or pick specific contacts from each address book to include in your Contact List.</p>
                                    <p>
                                        <img border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/importing_contacts/importer.png" alt=""></p>
                                    <p>
                                        <strong>Option 3:</strong> Import from the attendees of a past event in your Eventbrite account. Once you select an event (or multiple events) all attendees of the past event(s) will appear in the list of invitation recipients for this newest event, and you will have the option of removing any recipients before sending the invitation.</p>
                                    <p>
                                        <img border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/importing_contacts/past_events.png" alt=""><br>
                                        <strong>Option 4:</strong> Manually enter email addresses to include in your contact list.</p>
                                    <p>
                                        <img width="492" height="229" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/importing_contacts/manual_entry.png" alt=""></p>
                                </div>
                                <div class="step">
                                    <div class="stepcontent">
