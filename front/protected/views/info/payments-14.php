                        <!--step-->
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> If you've chosen to use PayPal to process payments for your event, your attendees are <strong>NOT</strong> required to have a PayPal account to complete payment. They can pay using their credit card, in an experience almost as smooth as our <a href="/en_us/t/credit-card-processing-faq">standard credit card processing</a>.</p>
                                <p>
                                    You, the event organizer, must set the <strong>PayPal Account Optional</strong> setting to&nbsp;<strong>On</strong> in your PayPal account to allow customers to pay by credit card without logging in to a PayPal account.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepcontent">
                                <p>
                                    <strong>Here's how to turn on this setting within your PayPal account:</strong></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    Log in to your <strong>PayPal</strong> Account <a target="_blank" href="http://www.paypal.com">here</a> and select the <strong>Profile</strong> tab near the top of the screen. <img width="613" height="153" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/PayPal-optional-setting/paypaloptionalsetting1.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Click on <strong>My selling tools</strong> on the left side of the page.&nbsp;&nbsp;</p>
                                <img width="251" height="264" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/PayPal-optional-setting/sellingtools22.png" alt="">
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Click on <strong>Update </strong>next to <strong>Website preferences</strong>.</p>
                                <img width="610" height="291" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/PayPal-optional-setting/websiteprefs22.png" alt="">
