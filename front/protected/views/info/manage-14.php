                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>Event organizers sometimes set up their Eventbrite event page to take registrations from people who purchased a voucher on a site like Groupon, LivingSocial, or HomeRun. Typically, organizers collect voucher codes from attendees using a custom question at checkout. See below for a look at how to set things up!</p>
