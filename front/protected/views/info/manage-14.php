                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To get started, first <a href="https://www.eventbrite.com/signup">sign up</a> for a free Eventbrite account or<a href="https://www.eventbrite.com/login"> log in</a> to your existing Eventbrite account and<a href="/en_us/t/event-page-creation"> create an event</a>.</p>
                                <p>
                                    If you’d like to edit an already-created event, click <strong>My Events</strong> at the top of the screen and then click on <strong>Edit </strong>to the right of your event’s name.</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 608px; height: 258px; " src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-use-vouchers-deals/edit2.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    In <strong>Step 2: Create Tickets</strong> on your <strong>Event Details</strong> page, click the&nbsp;<strong>Free</strong>&nbsp;button to the right of&nbsp;<strong>Add a Ticket</strong>&nbsp;to begin creating your free ticket.</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 607px; height: 183px; " src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-use-vouchers-deals/free.png" alt=""></p>
                                <p>
                                    Create a <strong>Ticket Name</strong> that indicates to the attendee they will use their voucher code when registering for this ticket type. An example is "Using a Voucher." Once you've named your ticket type, input the <strong>Ticket Quantity </strong>for this ticket type and leave the <strong>Ticket Price</strong> set to 0.00.</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 607px; height: 201px; " src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-use-vouchers-deals/voucher.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Then, click <strong>Make Event Live</strong> or <strong>Save</strong> in the upper right. Now click <strong>Manage</strong> in the upper left.</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 611px; height: 43px; " src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-use-vouchers-deals/save.png" alt=""></p>
                                <p>
                                    On the <strong>Manage </strong>page, select <strong>Customize Order Form</strong>, found near the top of the features menu on the left side of the page.</p>
                                <p>
                                    <img src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_survey/customizeorder.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    Choose the option to <strong>Collect information below for each attendee</strong>.</p>
                                <p>
                                    <img border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-use-vouchers-deals/attendee.png" alt=""></p>
                                <p>
                                    Next create a custom survey question by clicking <strong>Create a Question</strong>&nbsp;under <strong>Your Questions</strong>.&nbsp;</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 608px; height: 86px; " src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-use-vouchers-deals/add.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    Input your question in the <strong>Question</strong> field. This can be something along the lines of "Please enter your voucher number here." Then, designate the <strong>Question Type</strong> to be a <strong>Text</strong>.</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 609px; height: 298px; " src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-use-vouchers-deals/custom_q.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                6</div>
                            <div class="stepcontent">
                                <p>
                                    Under <strong>Optional Settings</strong>, check the box to the left of <strong>Show this question for specific ticket types</strong> and check the box to the left of “Using a Voucher” to show this question only for the voucher ticket type you created.</p>
                                <p>
                                    If you have more than one ticket type, you can select which tickets to apply the custom question(s) to. (You can learn more about creating ticket type-specific custom questions <a href="/en_us/t/ticket-typespecific-questions">here</a>.)</p>
                                <p>
                                    Be sure to click <strong>Save </strong>to save your changes!</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 609px; height: 232px; " src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-use-vouchers-deals/custom_q2.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                7</div>
                            <div class="stepcontent">
                                <p>
                                    On the next screen, select that this question be <strong>Required</strong>. Remember to click <strong>Save Changes</strong> once you're finished.</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 611px; height: 164px; " src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-use-vouchers-deals/required.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                8</div>
                            <div class="stepcontent">
                                <p>
                                    Before your event, you can compare the number of “Use a Voucher” tickets that have been “sold” or redeemed in Eventbrite with the number of deals or vouchers that you sold on the outside “daily deal” website to make sure that these two numbers sync.</p>
                                <p>
                                    If you’d like further details, you can run an <a href="/en_us/t/attendance-summary">Attendee Summary report</a> and export it to Excel. The Attendee Summary report will include the answers to all of the custom questions you asked your attendees, including the voucher numbers they redeemed. You can then cross reference the voucher numbers collected on Eventbrite with the numbers provided by the “daily deal” site.</p>
