                        <div class="block-heading">
                            <h3>Mark offline payment as received</h3>
                            <ul>
                                <li>
                                    Track who has gotten you their offline payment</li>
                                <li>
                                    Follow up with those whose offline payment is still due</li>
                                <li>
                                    Resend the order confirmation as receipt of payment received</li>
                            </ul>
                        </div>

                        <!--step-->
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>The Eventbrite <a href="http://eventbrite.com/fees/">fee</a> is charged on all orders with offline payments, regardless of whether the order is marked as received.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To find an order with an <a href="/en_us/t/offline-payment-options">offline payment</a> to mark as received, first go to your event's <strong>Manage </strong>page. To get there, click on <strong>My Events</strong> at the top of the screen, then click on the name of your event.</p>
                                <p>
                                    <img width="612" height="272" src="http://eventbrite-s3.s3.amazonaws.com/static/images/tutorials/how-to-mark-an-offline-payment-as-received/manage2.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Next, click on <strong>Event Reports</strong> on the left side of the <strong>Manage </strong>page.</p>
                                <p>
                                    <img width="231" height="199" border="0" src="http://eventbrite-s3.s3.amazonaws.com/static/images/tutorials/how-to-mark-an-offline-payment-as-received/event%20reports22.png" alt=""></p>
                                <p>
                                    Set the <strong>Report Type</strong> dropdown menu to <strong>Order Details</strong>. To find a specific order, you can search by the ticket buyer's name, email address or order number.</p>
                                <p>
                                    Or you can set the <strong>Order Type</strong> dropdown menu to <strong>Offline Payments - Payment Not Received</strong> to view all unpaid offline orders.</p>
                                <p>
                                    <img width="617" height="203" border="0" src="http://eventbrite-s3.s3.amazonaws.com/static/images/tutorials/how-to-mark-an-offline-payment-as-received/order_type2.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Once you find the order you're looking for, click the <strong>Mark</strong> <strong>as Received</strong> link.</p>
                                <p>
                                    <img width="620" height="278" border="0" src="http://eventbrite-s3.s3.amazonaws.com/static/images/tutorials/how-to-mark-an-offline-payment-as-received/mark_as_received.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>The order's color will change from yellow to gray, and can now be accessed by selecting <strong>Offline Payments - Payment Received</strong> from the <strong>Order Type</strong> dropdown menu. If you run and export an <a href="/en_us/t/order-details-report"><strong>Order Details</strong> report</a> to Excel, you'll find that the <strong>Status </strong>of the order has also changed to reflect that payment has been received.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    To send the attendee confirmation that their payment has been received, select <strong>Resend Confirmation Email </strong>from the top <strong>Quick Actions</strong> dropdown menu, to the right of the ticket buyer's information.</p>
                                <p>
                                    <img width="619" height="294" border="0" src="http://eventbrite-s3.s3.amazonaws.com/static/images/tutorials/how-to-mark-an-offline-payment-as-received/resend_conf_email2.png" alt=""></p>
                                <p>
                                    The <strong>Payment Status</strong> box on the PDF ticket that is attached to the order confirmation email will show that payment has been received and can serve as a receipt of payment.</p>
                                <p>
                                    <img width="614" height="369" border="0" src="http://eventbrite-s3.s3.amazonaws.com/static/images/tutorials/how-to-mark-an-offline-payment-as-received/tix2.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>PDF tickets must be enabled for your event in order for the attendee to receive the PDF ticket as described in step 4 above. Go <a href="/en_us/t/order-confirmation-customization">here</a> to learn more about enabling or disabling PDF tickets for your event.&nbsp;</p>
                            </div>
