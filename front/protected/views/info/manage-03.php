                                <p>
                                    <strong>Note: </strong>If you’ve created only one ticket type for this event, the number entered in the <strong>Ticket Quantity</strong> field will automatically appear in the <strong>Event Capacity</strong> field. As more ticket types are created, the ticket quantities will add together and the total will update in the<strong> Event Capacity</strong> field.</p>
