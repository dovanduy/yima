                                <img width="596" height="231" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/changing-quantity-of-tickets-available/changeticketquantity-new3.png" alt="How to create ticket types"><p>
                                    Once you do this, you’ll see that an <strong>Edit</strong> link to the right of the Event Capacity field appears. You can always click on this while your event is live to update the Event Capacity for your event. <img width="598" height="223" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/changing-quantity-of-tickets-available/changeticketquantity-new33.png" alt="How to create ticket types"></p>
