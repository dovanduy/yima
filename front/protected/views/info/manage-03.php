                            <div class="stepcontent">
                                <p>
                                    To update the <strong>Ticket Quantity</strong>, go to <strong>Step 2: Create Tickets</strong>, click into the <strong>Ticket Quantity</strong> box to the right of the <strong>Ticket Name</strong> that you’re trying to update and enter the new quantity of tickets available. Remember to <strong>Save</strong> your changes.</p>
                                <img width="595" height="240" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/changing-quantity-of-tickets-available/changeticketquantity-new2.png" alt="How to create ticket types">
