                                <a name="eventcapacity"></a>
                                <p>
                                    <strong>Note: Event Capacity</strong> sets the maximum number of tickets across <strong>all </strong>ticket types that can be sold for this event. This feature lets you create two or more ticket types--each with its own quantity available--and allows you to cap the total number of tickets sold for the event across all ticket types.</p>
                                <p>
                                    Event Capacity will override any number that you have entered for the Quantity Available of a particular ticket type. Event Capacity is accessed in <strong>Step 2: Add Ticket Information</strong> on the <strong>Edit </strong>page for your event.</p>
                                <p>
                                    <img width="520" height="154" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/changing-quantity-of-tickets-available/eventcapacity.png" alt=""></p>
                                <p>
                                    As an example, let's say you have a venue with a total capacity of 100 people. You're selling two ticket types--General Admission and VIP--and don't care about the mix of ticket types that you sell, as long as you don't sell more than 100 total tickets. In this case, you'd want to set the <strong>Quantity Available</strong> for each ticket type to 100, and then also set the <strong>Event Capacity</strong> to 100.</p>
