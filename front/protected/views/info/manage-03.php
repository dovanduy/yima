                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>You may have noticed that when editing your event, you’re able to set a <strong>Ticket Quantity</strong> and an <strong>Event Capacity</strong>. The difference between the two is explained <a href="/en_us/t/event-capacity-explained">here</a>.</p>
                                <p>
                                    The <strong>Event Capacity</strong> will override any number that you have entered for the <strong>Ticket Quantity</strong> of a particular ticket type. Both the Event Capacity and Ticket Quantity fields can be accessed in <strong>Step 2: Create Tickets</strong> on the <strong>Event Details</strong> page for your event.</p>
                            </div>
                        </div>
