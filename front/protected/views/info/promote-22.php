                            <div class="stepcontent">
                                <p>
                                    To get started, you'll need to copy the URL for your event page, so that later you'll be able to paste that URL into Facebook's Debug tool (which we explain how to do in step 2 below).</p>
                                <p>
                                    You can find the URL for your event page by logging in to your Eventbrite account, clicking on <b>My Events</b> at the top of the page, then clicking <b>View</b> to the right of your event's name (under <b>Quick Links</b>) to go to your event page. Copy the URL for your event page from your browser's address bar.</p>
