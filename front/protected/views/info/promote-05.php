                            <div class="stepnumber">
                                <img alt="Note" src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>The <strong>Publish to Facebook</strong> option will not appear for <a href="/en_us/t/repeating-events">repeating events</a>. If you would like to publicize your repeating event on Facebook, you will need to manually create an event directly in Facebook.</p>
                                <p>
                                    You can also check out the third-party Facebook apps <a href="http://appbistro.com/products/eventbrite#">Eventbrite for Pages</a> (read more about it <a href="http://blog.eventbrite.com/sell-tickets-through-facebook-version-two-of-eventbrite-for-pages-launches/">here</a>) and <a href="http://eventbrite.appstores.com/apps/sell-tickets-on-facebook">GroSocial</a> (read more about it <a href="http://blog.eventbrite.com/sell-tickets-on-facebook/">here</a>).</p>
