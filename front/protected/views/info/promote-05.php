                        <!-- Step 1 -->		<div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    After publishing your event on Eventbrite, click on <strong>My Events </strong>on the top of the page. Clicking on the name of the event will redirect you to the <strong>Manage</strong> page for your event. Select <strong>Publish to Facebook</strong> from the features menu of the left side of the page.</p>
                                <p>
                                    <img alt="" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/promote_on_facebook/features%20menu.png" border="0"></p>
                            </div>
                        </div>
                        <!-- Step 1 -->		<div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Now click <strong>Connect with Facebook</strong>.</p>
                                <p>
                                    <img alt="" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/promote_on_facebook/connect.png" border="0" height="154" width="610"></p>
                            </div>
                        </div>
                        <!-- Step 1 -->		<div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    If you're not already logged in to Facebook, a <strong>Connect with Facebook</strong> screen will appear. Log in using your Facebook log-in email and password.</p>
                                <p>
                                    (If you are already logged in to Facebook, skip to step 5 below.)</p>
                                <p>
                                    <img alt="" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/promote_on_facebook/login.png" border="0" height="285" width="612"></p>
                            </div>
                        </div>
                        <!-- Step 1 -->		<div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    If you've never before accessed Facebook from within your Eventbrite account, you'll need to grant permission to Eventbrite to publish and update your event details on Facebook. (Don't worry, we'll never publish an event on Facebook unless you explicitly tell us to.)</p>
                                <p>
                                    <img alt="" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/promote_on_facebook/permission.png" border="0" height="290" width="612"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    Once you're successfully logged in to Facebook, your basic event details--event name, date/time, location, category, description--will appear. You can edit or add new information to the event name and/or description fields to modify how the event will appear in Facebook. (Facebook controls which fields are editable, so if you would like to edit any other fields, you would need to do so directly from the <strong>Edit</strong> page of your event in Eventbrite.)</p>
                                <p>
                                    If you are the administrator of a fan or group page on Facebook, you can choose to publish to either of these pages, or to your personal page. (Facebook only allows you to publish to one of these pages directly from within Eventbrite. If you'd like to publish to more than one page, you'd need to manually create an event and publish to those additional pages directly from within Facebook.)</p>
                                <p>
                                    When you're finished, click <strong>Publish to Facebook</strong>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img alt="Note" src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>Facebook requires that your event have a physical address in order to publish it to Facebook directly from within Eventbrite. This means that you must specify a physical address for your event on your Eventbrite event page. If you have marked your event as an 'online event' in Eventbrite, you'll need to manually create the Facebook event in Facebook, and then include a link to your Eventbrite event page in the Facebook event's description.&nbsp;</p>
                                <p>
                                    <img alt="" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/promote_on_facebook/publish.png" border="0" height="612" width="608"></p>
