                                <p>
                                    Look to <strong>End Sales</strong> and select the date and time you'd like the "early bird" special price to end. For example, if you'd like the tickets to stop selling once a day is over, you'll want to select the date that follows it at midnight.&nbsp;</p>
                                <p>
                                    You may also want to leave a <strong>Ticket Description</strong> to provide more information for and promote the "early bird" ticket.</p>
                                <p>
                                    <img width="590" height="635" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/early_bird_pricing/earlybird23.png" alt="How to create early bird pricing"></p>
