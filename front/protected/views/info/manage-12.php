                                    Now, if you haven't already, you'll want to create a general admission ticket.</p>
                                <p>
                                    Repeat the steps above, but this time select a <strong>ticket type start sales date/time</strong>.</p>
                                <p>
                                    This will indicate when the general admission ticket type should go on sale (generally right after the "early bird" ticket expires).</p>
