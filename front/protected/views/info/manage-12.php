                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    In <strong>Step 2: Add Ticket Information</strong>, click on the <strong>Create a Ticket</strong> button (or on the&nbsp;<strong>Add a New Ticket</strong> button if you have already created at least one ticket).</p>
                                <p>
                                    <img width="567" height="188" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/early_bird_pricing/earlybird2.png" alt="How to create early bird pricing"></p>
                                <p>
                                    Enter the <strong>Ticket Name</strong>, <strong>Price</strong>, and <strong>Quantity Available</strong> of your "early bird" ticket. Then click <strong>Show</strong> to see the <strong>Advanced Options.</strong></p>
                                <p>
                                    <img width="559" height="294" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/early_bird_pricing/earlybird21.png" alt="How to create early bird pricing"></p>
