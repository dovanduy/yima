                        <div class="block-step">
                            <div class="block-heading">
                                <h3>Early-bird pricing</h3>
                                <ul>
                                    <ul>


                                        <li>
                                            Reward early buyers with a discount</li>
                                        <li>
                                            Create a sense of urgency</li>
                                        <li>
                                            Drive earlier sales for your peace of mind</li>

                                    </ul>
                                </ul>
                            </div>
                        </div>

                        <div class="step">
                            <div class="stepnumber">
                                <img border="0" src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>This tutorial will help you set up a ticket that has a special price for those of your attendees who register early for your event. To learn more about creating general ticket types, click <a href="/en_us/t/ticket-type-creation">here</a>.</p>
                                <p>
                                    Click <a href="http://www.eventbrite.com/event/2203101536">here</a> to take a look at an example of an event with an "early bird" ticket type that you'll learn to create here!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To get started, first <a href="https://www.eventbrite.com/signup">sign up</a> for a free Eventbrite account or<a href="https://www.eventbrite.com/login"> log in</a> to your existing Eventbrite account and<a href="/en_us/t/event-page-creation"> create an event</a>.</p>
                                <p>
                                    If you’d like to edit an already-created event, click <strong>My Events</strong> at the top of the screen and then click on <strong>Edit </strong>to the right of your event’s name.</p>
                                <p>
                                    <img width="613" height="272" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/early_bird_pricing/edit2.png" alt="How to create early bird pricing"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    In&nbsp;<strong>Step 2: Create Tickets</strong>, first create your standard-price ticket--called "General Admission" in our example--if you haven't already.</p>
                                <p>
                                    Then, to create your "early bird" ticket type, select <strong>Paid ticket</strong> from the <strong>Add Ticket Type </strong>dropdown menu.</p>
                                <p>
                                    Enter the <strong>Ticket Name </strong>(called "Early Bird" in our example), <strong>Ticket Quantity</strong>, and a <strong>Ticket Price</strong>--cheaper than your standard-price ticket--for your "early bird" ticket.</p>
                                <p>
                                    <img width="609" height="254" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/early_bird_pricing/early.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Now click <strong>Settings</strong> for the Early Bird ticket type. Set the <strong>Sale Starts </strong>and <strong>Sale</strong><strong> Ends</strong> dates/times for this ticket. Consider also including a <strong>Ticket description</strong> alerting people when the price will go up, so as to persuade them to buy earlier at the lower price (see the image below for our example text).</p>
                                <p>
                                    Once you've finished, click <strong>Settings</strong> again to close the settings for your Early Bird ticket.</p>
                                <p>
                                    <img width="612" height="349" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/early_bird_pricing/settings.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    The last thing to do is to set the sale of General Admission tickets to automatically start right when the sale of Early Bird tickets has ended.</p>
                                <p>
                                    To do this, click&nbsp;<strong>Settings</strong> for the General Admission ticket type. Next to&nbsp;<strong>Sale Starts</strong>, click&nbsp;<strong>or when ticket sales end</strong>.&nbsp;</p>
                                <p>
                                    <img width="613" height="278" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/early_bird_pricing/when.png" alt=""></p>
                                <p>
                                    Select <strong>Early Bird</strong> from the <strong>When sales end for</strong> dropdown menu.&nbsp;</p>
                                <p>
                                    <img width="612" height="339" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/early_bird_pricing/settings2.png" alt=""></p>
                                <p>
                                    You'll see the <strong>Sales Status </strong>of the General Admission ticket change from <strong>On</strong> <strong>Sale </strong>to <strong>Not Started</strong> when you do so.</p>
                                <p>
                                    <img width="613" height="253" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/early_bird_pricing/status.png" alt=""></p>
                                <p>
                                    Once you've finished, click <strong>Settings</strong> again to close the settings for your General Admission ticket.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    To see how your event page will look, click <strong>Colors &amp; Options </strong>in the upper left. See that the <strong>Ticket Information</strong> box shows both ticket types, the ticket description for the Early Bird ticket, and that only the Early Bird ticket is on sale right now.</p>
                                <p>
                                    <img width="533" height="303" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/early_bird_pricing/page2.png" alt=""></p>
                                <p>
                                    Once sale of the Early Bird ticket type ends--at the date and time that you set in step 3 above--the sale of the General Admission ticket type will start, per the steps you followed in step 4 above. Well done!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img border="0" src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>If you only want to have one ticket to appear at a time, you can manually hide the other ticket type. To do this,&nbsp;go to <strong>Step 2: Create Tickets</strong> on your event's <strong>Event Details</strong> page.&nbsp;In the <strong>Sales Status</strong> column, choose <strong>Hidden </strong>from the dropdown menu for that ticket type, which means that the people who view your event page won't be able to see it.</p>
                                <p>
                                    <img width="612" height="250" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/early_bird_pricing/hidden.png" alt=""></p>
                                <p>
                                    To make the ticket type appear again, you can choose <strong>Not Started</strong> (which means the <strong>Quantity</strong> column on your event page will show as <strong>N/A</strong>) or <strong>On Sale</strong>, depending on the <strong>Sale Starts</strong> date/time that you've set for this ticket type in its <strong>Settings</strong>.</p>
                            </div>
