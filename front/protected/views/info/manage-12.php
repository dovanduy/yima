                                    <strong>Note: </strong>If you only want to have one ticket to appear at a time, you can manually hide a ticket type. To do this, you'll want go to the <strong>Edit</strong> section of your event and look to <strong>Step 2: Add Ticket Information</strong>. To the right of the name of your ticket type you'll want to click the <strong>hide</strong> link. Once the link is clicked, the status of the ticket type will read <strong>hidden</strong>, meaning the registrants who view your event page won't be able to see it.&nbsp; To make the ticket type appear again, you can click on the <strong>show</strong> link that appears to the right of the ticket type name.</p>
