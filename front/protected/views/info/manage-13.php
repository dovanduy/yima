                                <p>
                                    When viewing your event page, go to the <strong>Ticket Information </strong>box and click on the <strong>Quantity</strong> dropdown menu for your "Table for 10" ticket type. You'll see that the guest can only choose to order the number of tickets that you set in the <strong>Min. Tickets per Order</strong> and the <strong>Max. Tickets per Order</strong> fields in step 4 above.</p>
                                <p>
                                    <img width="623" height="349" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-setup-tabled-seating/tabedseating51.png" alt=""></p>
