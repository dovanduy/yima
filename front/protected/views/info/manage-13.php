                            <span style="font-size: large;"><strong>Setting up the ticket type</strong></span></p>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To get started, <a href="https://www.eventbrite.com/login">log in</a> to your Eventbrite account. To create a new event, click on the <a href="http://www.eventbrite.com/create"><strong>Create Event</strong></a> tab found at the top of the page. To edit an existing event, click on the <strong>My Events</strong> tab at the top of the screen and then click on <strong>Edit</strong> to the right of the name of your event.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    In <strong>Step 2: Add Ticket Information</strong>, click on the <strong>Create a Ticket</strong> button if this is your first ticket, or on the <strong>Add a New Ticket</strong> button if you have already created at least one ticket.</p>
                                <p>
                                    <img width="617" height="219" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-setup-tabled-seating/tabled_seating1.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Enter the <strong>Ticket Name</strong>, <strong>Price</strong>, and <strong>Quantity Available</strong>.</p>
                                <p>
                                    As an example, let's say you'd like to sell a table for $1000 dollars, have 10 seats available per table, and have a total of 20 tables available. In this case, you'd set the <strong>Price</strong> to $100 (this is the per-seat price) and set the <strong>Quantity Available</strong> to 200 (this is the total number of seats that are available across all tables).</p>
                                <p>
                                    <img width="617" height="315" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-setup-tabled-seating/create_ticket.png" alt=""></p>
                                <p>
                                    Once you've entered the information above, click <strong>Show</strong> next to <strong>Advanced Options </strong>to set the minimum and maximum number of tickets per order.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    Enter the number of tickets (in terms of seats) available at the table in both the <strong>Min. Tickets per Order</strong> and the <strong>Max. Tickets per Order</strong> fields. (For our example, you'd want to set both fields to 10.)</p>
                                <p>
                                    You may want to include a <strong>Ticket Description</strong> explaining what your attendee will receive by purchasing this ticket. Click the <strong>Save</strong> button to save your changes.</p>
                                <p>
                                    <img width="616" height="671" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-setup-tabled-seating/create_ticket2.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
