                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>The price that you set you for your ticket will be multiplied by the number of tickets ordered, equaling the total price of the table package. (In our example, the attendee will be required to purchase 10 of the "Table for 10" tickets at $100 a piece, which equals the $1000 that we're charging for the entire table.)</p>
