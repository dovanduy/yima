                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> The maximum file size of an image to be uploaded into the<strong> Event Details </strong>section is 1 megabyte. If your image is larger than this, first use an outside image editor to decrease its size, then follow the steps below to insert it into your event page. The <strong>Event Details </strong>section is 600 pixels wide, so you'll want to make sure that any image you upload is no wider than this (though it can be any height).</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    From the <strong>My Events</strong> page, click on <strong>Edit </strong>next to the name of an event to go to the <strong>Edit </strong>page for that event. (If you are setting up a new event page--that is, you just pressed <strong>Create Event</strong> and the page you are on says <strong>Create an Event</strong>--you can skip this step and go directly to step 2 below.)</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    In <strong>Step 3: Add Event Details</strong>, click the button on the toolbar that has a picture of a tree. (It will say 'Insert/edit image' when you position your cursor over it.)</p>
                                <p>
                                    <img width="576" height="145" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-add-images-to-event-page/Step3%20header.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    In the pop-up window labeled 'Insert/edit image,' click the orange <strong>Browse</strong> button.</p>
                                <p>
                                    <img width="406" height="314" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-add-images-to-event-page/firstimageuploader.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    In the next pop-up window, labeled 'Tiny Browser,' click the gray <strong>Browse</strong> button<strong>. </strong></p>
                                <p>
                                    <strong><img width="513" height="332" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-add-images-to-event-page/tinybrowser.png" alt=""></strong></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    Find the image on your computer that you want to add, select it, and click <strong>Open</strong>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                6</div>
                            <div class="stepcontent">
                                <p>
                                    The name of your image file and its size and type should now be showing in the 'Tiny Browser' window. Click <strong>Upload Files</strong>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                7</div>
                            <div class="stepcontent">
                                <p>
                                    On the next screen, find your image among those that you have uploaded and click on it. Your image will appear in the <strong>Preview</strong> section of the 'Insert/edit image' window. Click <strong>Insert</strong>.</p>
                                <p>
                                    <img width="403" height="341" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-add-images-to-event-page/insert.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                8</div>
                            <div class="stepcontent">
                                <p>
                                    Your image is now in the <strong>Event Details</strong> box. Well done! You can move the image around by cutting/pasting it within the box. You can also center or align the image by using the text-alignment buttons in the HTML toolbar after highlighting the image.</p>
                                <p>
                                    <img width="390" height="380" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/how-to-add-images-to-event-page/editimage.png" alt=""></p>
                            </div>
