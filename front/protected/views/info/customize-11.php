                        <div class="step">
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> These are a few ways you can convert your .pdf file to an image file for use in your event page. If someone designed your flier for you, you may also contact them to see if they can provide you with an image file (.jpeg, .jpg, .gif, or .png) instead. For more on inserting images into your event details, click <a href="/en_us/t/images-for-event-page">here</a>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Option 1 (the easiest): convert your PDF into an </strong><strong>image file</strong></p>
                                <p>
                                    There are several ways to convert a .pdf into an image file. One way is a free web-based service called <a href="http://imgur.com/">Imgur </a>that will allow you to convert your .pdf to an image.</p>
                                <p>
                                    The first step is to upload your .pdf to Imgur. Once it's uploaded, right click on<strong> </strong>the preview image and click <strong>save as</strong> to save the image to your desktop. This will save your new .jpg file to your computer's hard drive. To give it a try, click <a href="http://imgur.com/">here</a>.</p>
                                <p>
                                    Once you have your .jpg file, go <a href="/en_us/t/images-for-event-page">here</a> to learn how to insert it into your event details.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Option 2: take a screenshot of the PDF and save as an image file</strong></p>
                                <p>
                                    The best way to take a screenshot will depend on your computer's operating system. Click <a href="http://take-a-screenshot.org/">here</a> to learn how to take a screenshot.</p>
                                <p>
                                    Once you have your .jpg file, go <a href="/en_us/t/t/how-to-add-images-to-event-details">here </a>to learn how to insert it into your event details.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    <strong><strong>Option 3 (advanced): use Scribd to embed the PDF in your event details</strong> </strong></p>
                                <p>
                                    The final and slightly more involved option is a web service called Scribd. Scribd is a free web service where you can host .pdf files and that then will auto-generate the HTML code you need in order to embed .pdf files in your event page.</p>
                                <p>
                                    - To embed a .pdf using Scribd, go to <a target="_blank" href="http://www.scribd.com/">http://www.scribd.com</a>.</p>
                                <p>
                                    - Click the <strong>Upload</strong> button to choose your file to upload and then upload it to Scribd. You’ll be prompted to enter your email address to create an account on Scribd.</p>
                                <p>
                                    - On Scribd’s <strong>My Documents</strong> page, and click on the name of the document you wish to embed.</p>
                                <p>
                                    - Once your document opens, click <strong>Embed</strong> in the <strong>Share &amp;amp; Embed </strong>section. A dialog box will open. Click on the tab marked <strong>Flash</strong>. (Note: Clicking the<strong> Flash</strong> option is very important.)</p>
                                <div>
                                    <img width="500px" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/add-PDF-to-event-page/scribdshare.png" alt="">
                                </div>
                                <div>
                                    <img width="500px" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/add-PDF-to-event-page/scribdflash.png" alt="">
                                </div>
                                <p>
                                    - Click the <strong>Copy</strong> button to copy this embed code to your clipboard.</p>
                                <p>
                                    - Finally, go back to Eventbrite and bring up the <strong>Edit</strong> page for the event you are working on. In <strong>Step 3: Add Event Details</strong>, click on the <strong>HTML</strong> button in the toolbar. <img width="595" height="500" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/add-PDF-to-event-page/htmlbutton.png" alt=""><br>
                                    - Once the <strong>HTML Source Editor</strong> window opens, paste the embed code from your clipboard into the HTML code for your page. Now click <strong>Update</strong> at the bottom of the <strong>HTML Source Editor</strong>. Remember to save your changes before leaving the <strong>Edit </strong>page!<br><br><br>
                                    &nbsp;</p>
                            </div>
