                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>Tax is charged on the total price the attendee will pay at checkout, inclusive of <a href="/en_us/t/feehandling-options">fees</a>. By default, tax is not included in the ticket price and is added on top of the ticket price, though you can choose to include it in the ticket price instead.</p>
