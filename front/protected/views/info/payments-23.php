                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>If you've already sold a ticket for your event, you will be unable to start charging tax for the event. If you've sold a ticket and want to charge tax, you can <a href="/en_us/t/copy-an-event-page">copy your existing event page</a> and then enable tax support for the new event page.</p>
