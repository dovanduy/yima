                                <p>
                                    (<strong>We'll use a ticket priced in GBP for an event on our UK [.co.uk] site for our example. If you're on a non-UK site, what you see may be a bit different.</strong>)</p>
                                <p>
                                    Next click <strong>+ Add VAT</strong>.</p>
                                <p>
                                    <img width="616" height="281" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/charge-VAT-on-your-ticket-sales/add%20VAT.png" alt=""></p>
                                <p>
                                    In the <strong>Tax Options</strong> window, select <strong>Yes</strong> to show all fields and options to fill out.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    First complete the three required fields:</p>
                                <p>
                                    - The name of the tax, as it will appear on the <a href="/en_us/t/order-confirmation-customization">order confirmation email and PDF ticket</a> that your attendees will receive after ordering. See below for how this will display. (We'll use "VAT" for our example.)</p>
                                <p>
                                    - The percentage tax that you would like to charge.</p>
                                <p>
                                    - Your tax registration ID.</p>
                                <p>
                                    Then choose the ticket type(s) to which you want to apply this tax.</p>
                                <p>
                                    Last, choose whether to include the tax in the ticket price, then click <strong>Save</strong>.<br>
                                    &nbsp;</p>
                                <p>
                                    <img width="610" height="573" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/charge-VAT-on-your-ticket-sales/taxoptions22.png" alt=""></p>
                            </div>
