                                </div>
                                <div class="stepcontent">
                                    <p>
                                        <strong>Note: </strong>If you'd like to limit the number of tickets for which an attendee can register in an order, you can do that when setting up the <a href="/en_us/t/ticket-type-creation">ticket type(s)</a> for your event.&nbsp;</p>
                                </div>
                            </div>
                            <div class="step">
                                <div class="stepnumber">
                                    5</div>
                                <div class="stepcontent">
                                    <p>
                                        On the <strong>Registration</strong> <strong>Information</strong> page, where an attendee enters their information once they have selected a number of tickets, the email address to which you sent their invitation is hard-coded in and cannot be changed. This ensures that only those that you have invited can register for your event.<br>
                                        &nbsp;</p>
                                    <p>
                                        <img width="582" height="430" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/invite-only-feature/inviteonly4.png" alt=""></p>
