                                        Once the invitation has been used to register for the event, it will not be able to be used&nbsp; to place another order. If the invitation is clicked on again, the invitee will see the message: "We're sorry, this invitation has already been used."<br>
                                        &nbsp;</p>
                                    <p>
                                        <img width="597" height="363" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/invite-only-feature/inviteonly7.png" alt=""></p>
