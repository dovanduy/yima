                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>If you select the invite-only feature, <em>only guests who receive an invitation created within Eventbrite will be able to register for your event</em>. The invite-only feature does <em>not </em>work in conjunction with invitations sent from MailChimp, Paperless Post or any other outside email client or service. If you would like to use the invite-only feature for your event, you will need to create and send your email invites directly from within Eventbrite.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To set your event to invite-only, from the <strong>My Events</strong> page, click on <strong>Edit </strong>next to the name of an event to go to the <strong>Edit </strong>page for that event. (If you are setting up a new event page--that is, you just pressed <strong>Create Event</strong> and the page you are on says <strong>Create an Event</strong>--you can skip this step and go directly to step 2 below.)<br>
                                    &nbsp;</p>
                                <p>
                                    <img width="612" height="329" border="0" title="Send Email Invitations" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/invite-only-feature/my%20events.png" alt="Send email invitations"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Scroll down to <strong>Step 8: Set Privacy</strong> and select the option "This event is private and should not be listed in the directory or on search engines"&nbsp; to classify your event as private. To activate the invite-only feature, then click the checkbox next to "<label for="invites_only">Event is invite-only and attendees MUST receive an Eventbrite invitation to register.</label>" Click the <strong>Save Changes </strong>button at the bottom right.&nbsp;<br>
                                    &nbsp;</p>
                                <p><img width="607" height="152" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/invite-only-feature/inviteonly2.png" alt=""></p>
                            </div>
                            <div>
                                &nbsp;</div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Once you’ve selected the invite-only privacy setting, we place a large notification message at the top of the <strong>Event Summary</strong> on the <strong>Manage </strong>page for your event to highlight that invitations must be sent for attendees to register.</p>
                                <p>
                                    Click <strong>Create Invitations </strong> in the notification message or on the features menu on the left side of the page to format your <a href="/en_us/t/email-invitations">email invitations</a> and select your guest list and then send your invitations.</p>
                            </div>
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p><img width="614" height="333" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/invite-only-feature/inviteonly3.png" alt=""></p>
                            </div>
                            <div class="step">
                                <div class="stepnumber">
                                    4</div>
                                <div class="stepcontent">
                                    <p>
                                        An invitation for an invite-only event will contain the wording "Event registration is by invite only. Register with your email address to attend this event." When an attendee receives an invitation, they <em>must</em> click the <strong>Attend</strong> button in the invite in order to go to your event page and be able to register. Once on the event page, they are able to select any number of tickets and then register for your event.&nbsp;<br>
                                        &nbsp;</p>
                                    <p><img width="593" height="998" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/invite-only-feature/inviteonly5.png" alt=""></p>
