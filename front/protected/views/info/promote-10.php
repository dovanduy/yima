                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> For more information about promoting your published event, click <a href="/en_us/t/promotional-tools-overview">here</a>!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To get started, go to your event's <strong>Manage</strong> page by clicking on <strong>My Events</strong> at the top of the page, and then clicking on the name of your event. Once on the <strong>Manage </strong>page, select<strong> Track Attendee Activity</strong> on the features menu on the left side of the page.</p>
                                <p>
                                    <img alt="Use Tracking Links" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/track_affiliate_links/promocode3.png" title="Use Tracking Links"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    You can create a new tracking link by first entering a <strong>Name</strong> for your tracking link. As you type, you'll see that a unique tracking link is created in the box below. Copy this link to your clipboard, and distribute it to the relevant promoter or marketing outlet. Then click <strong>Create</strong>.&nbsp;</p>
                                <img alt="How to create and manage affiliate tracking links" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/track_affiliate_links/create_link.png" border="0"><p>
                                    When the tracking link is used for the first time, a statistical box will appear on the <strong>Attendee Tracking Activity</strong> page in the <strong>Your tracking links</strong> section that will track the <strong>number of visits</strong>, <strong>tickets sold</strong> and <strong>amount collected</strong> for that particular tracking link.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img alt="Note" src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>If you've created a <a href="/en_us/t/custom-urlweb-address-for-event-page">custom URL</a> for your event, you'll see that two boxes appear when you are creating a tracking link. You may copy either link and distribute it to your promoter since both links will lead visitors to your event page.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    You can check on the progress of the existing tracking links in your event by clicking on <strong>Track Attendee Activity</strong> from the&nbsp;<strong>Manage</strong> page of your event. There, you can view a list of all the tracking links in your event and the number of <strong>visits</strong>, <strong>tickets sold</strong>, and total <strong>sales</strong>, that each has generated. You can view an <a href="/en_us/t/attendance-summary">Attendee Summary report</a> by clicking on <strong>Attendee Report </strong>from the <strong>Quick Actions</strong> dropdown menu to the right of the name of the tracking link.</p>
                                <p>
                                    At the top are links that have been automatically created by Eventbrite to generate even more sales without additional effort from you. Below this are any tracking links that you have created, as well as the button to create a new link.</p>
                                <p>
                                    <img alt="How to create and manage affiliate tracking links" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/track_affiliate_links/links.png" height="575" width="610"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img alt="Note" src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>Here's a guide to the codes that may appear in your list of Eventbrite-generated links. If you see any of these codes listed for your event, you'll know the visits and revenue that Eventbrite drove to your event without any additional effort from you!</p>
                                <p>
                                    - <strong>atom</strong>: Eventbrite RSS feeds</p>
                                <p>
                                    - <strong>efblike</strong>: Facebook news feed from an attendee liking an event</p>
                                <p>
                                    - <strong>efbnen</strong>: Facebook news feed event name link</p>
                                <p>
                                    - <strong>efbnreg</strong>: Facebook news feed register link</p>
                                <p>
                                    - <strong>eorg</strong>: Eventbrite <a href="/en_us/t/organizer-profile-pages">organizer page</a></p>
                                <p>
                                    - <strong>eorgf</strong>: featured event on your Eventbrite <a href="/en_us/t/organizer-profile-pages">organizer page</a></p>
                                <p>
                                    - <strong>erec</strong>: Eventbrite recommended your event to attendees in their Eventbrite account</p>
                                <p>
                                    - <strong>esli</strong>: LinkedIn share link on your event page</p>
                                <p>
                                    - <strong>estw</strong>: Twitter share link on your event page</p>
                                <p>
                                    - <strong>etckt</strong>: <a href="/en_us/t/embed-ticket-form-into-your-website">ticket widget </a></p>
                                <p>
                                    - <strong>eweb</strong>: Eventbrite <a href="/en_us/t/embed-registration-form-into-your-website">website integration</a></p>
                                <p>
                                    - <strong>incompleteregistration</strong>: automatic email sent when a registration isn't completed</p>
                                <p>
                                    - <strong>rss</strong>: Eventbrite RSS feeds</p>
                                <p>
                                    - <strong>auto</strong>: event search auto-suggest</p>
                                <p>
                                    - <strong>autohome</strong>: event search auto-suggest on our homepage</p>
                                <p>
                                    - <strong>connect</strong>: <a href="/en_us/t/publish-event-to-facebook">Facebook connect</a></p>
                                <p>
                                    - <strong>eivtefrnd</strong>: 'invite friend' link on your event page</p>
                                <p>
                                    - <strong>estwbreg</strong>: Twitter tweet button link</p>
                                <p>
                                    - <strong>zvents</strong>: Zvents events auto listing</p>
                                <p>
                                    - <strong>eotw</strong>: links from Twitter to your Eventbrite <a href="t/organizer-profile-pages-and-the-my-profile-tab">organizer page</a></p>
                                <p>
                                    - <strong>eoln</strong>: links from LinkedIn to your Eventbrite <a href="t/organizer-profile-pages-and-the-my-profile-tab">organizer page</a></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    To take a quick look at the progress of the Eventbrite-generated Tracking Links, you can simply scroll down in your event's <strong>Event Summary </strong>page until you see the <strong>Eventbrite Promotion Sources</strong> box. There you'll see the list of tracking links that we've generated and how much money they've helped you make in gross ticket sales.</p>
                                <p>
                                    <img alt="How to create and manage affiliate tracking links" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/track_affiliate_links/eventsumm.png" width="600"></p>
                                <p>
                                    You can always view the entire list of tracking links by clicking <strong>Track attendee activity</strong>.</p>
