                        <div class="block-heading">
                            <h3>Track my promotional activities</h3>
                            <ul>
                                <li>
                                    Create unique tracking link for each promoter or marketing outlet</li>
                                <li>
                                    Access visits, tickets sold and sales for each link</li>
                                <li>
                                    Use data to strategically apply your promotional muscle</li>
                                <li>
                                    See benefits of promotion Eventbrite does for you automatically</li>
                            </ul>
                        </div>

