                        <p class="center">
                            <strong><span style="font-size: large;">Enable and configure auto-assignment of numbers</span></strong><br>
                            &nbsp;</p>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To get started, first <a href="https://www.eventbrite.com/signup">sign up</a> for a free Eventbrite account or<a href="https://www.eventbrite.com/login"> log in</a> to your existing Eventbrite account and<a href="/en_us/t/event-page-creation"> create an event</a>.</p>
                                <p>
                                    If youâ€™d like to edit an already-created event, click <strong>My Events</strong> at the top of the screen and then click on <strong>Edit</strong> to the right of your event's name to go to your event's <strong>Event Details</strong> page and get started.</p>
                                <p>
                                    <img width="615" height="294" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/auto-assign-participant-numbers/edit.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    On your event's&nbsp;<strong>Event Details</strong> page, go to&nbsp;<strong>Step 3: Promote your Event Page</strong> (or&nbsp;<strong>Privacy Settings</strong>). Make sure your event is set to <strong>Public</strong>, and that one of the&nbsp;<strong>Search Categories</strong> is set to&nbsp;<strong>Endurance</strong>.</p>
                                <p>
                                    Click <strong>Save</strong> or <strong>Mak</strong><strong>e Event Live</strong> when you're finished.</p>
                                <p>
                                    <img width="572" height="250" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/auto-assign-participant-numbers/endurance.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Then go to your event's <strong>Manage</strong> page by clicking <strong>Manage</strong> in the upper left.</p>
                                <p>
                                    <img width="607" height="321" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/auto-assign-participant-numbers/manage.png" alt=""></p>
                                <p>
                                    If you'd like to configure your auto-assigned numbering sequences based on gender and/or age and/or one custom question that you create (e.g., starting time), click <strong>Customize Order Form</strong>, found on the features menu on the left side of the page, and continue with the directions in this step.</p>
                                <p>
                                    Otherwise, continue to step 1 in the next section below.</p>
                                <p>
                                    <img src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_survey/customizeorder.png" alt=""></p>
                                <p>
                                    On the <strong>Customize Order Form</strong> screen, first make sure that the option to <strong>Collect information below for each atendee</strong> is selected and the checkbox next to each of your ticket types is checked.</p>
                                <p>
                                    <img width="611" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/auto-assign-participant-numbers/bibcustomizeorder.png" alt=""></p>
                                <p>
                                    Scroll down to the bottom of the <strong>Information to collect</strong> screen and make <strong>Gender </strong>and/or <strong>Age</strong> (depending which you'd like to use to determine your numbering sequences) required questions for your attendees to answer at registration by checking both checkboxes next to each item.</p>
                                <p>
                                    <img width="273" height="192" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/auto-assign-participant-numbers/genderage.png" alt=""></p>
                                <p>
                                    If you'd like to create a custom question--e.g., starting time--to use to determine your numbering sequences, click <a href="/en_us/t/custom-questions-for-attendees">here</a> to learn how.</p>
                                <p>
                                    Make sure to click <strong>Save Changes</strong> at the top or bottom of the page once you're finished.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> Only age, gender and one custom question can be used to determine numbering sequences. Feel free, though, to collect more than just this information from your attendees--though the additional information won't be used to determine your numbering sequences. For more information about collecting info from your attendees, click <a href="/en_us/t/collect-info-from-each-attendee">here</a>.</p>
                                <p>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p class="center">
                            <strong><span style="font-size: large;">Create numbering sequences</span></strong><br>
                            &nbsp;</p>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To start creating numbering sequences, click <strong>Auto-Assign Numbers</strong>, found at the bottom of the features menu on the left side of the <strong>Manage</strong> page.</p>
                                <p>
                                    <img width="231" height="175" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/auto-assign-participant-numbers/auto-assign.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    To create a sequence, give it a name, indicate a minimum number of digits for the sequence (optional), the minimum starting number for the sequence, and any numbers to exclude from the sequence (optional).</p>
                                <p>
                                    Choose which ticket type(s) the sequence should include and then whether the sequence should be limited to an age range and/or gender and/or responses to one custom question you created. (See step 3 in the section above if you don't see&nbsp;<strong>Gender</strong> and&nbsp;<strong>Age</strong> as options here--or would like to create a custom question--and require them as options.)</p>
                                <p>
                                    <img width="606" height="482" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/auto-assign-participant-numbers/sequence.png" alt=""></p>
                                <p>
                                    Remember to click <strong>Save Changes</strong>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Once you've created a sequence, you'll see a summary of that sequence, including <strong>Min</strong> (minimum number for the sequence), <strong>Excluded</strong> (any numbers excluded from the sequence), and how many attendees were assigned to the sequence. You'll also see the number of unassigned attendees--i.e., registered attendees who don't fit into any of the sequences that you've created--for each of your ticket types.</p>
                                <p>
                                    For more information about the sequence criteria you've set, click <strong>Settings</strong>.</p>
                                <p>
                                    <img width="606" height="348" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/auto-assign-participant-numbers/sequenceex1.png" alt=""></p>
                                <p>
                                    Any attendees fitting the criteria for this sequence who register from this point forward will be auto-assigned to this sequence.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> If an attendee happens to fit into two or more sequences, the attendee will be assigned to the oldest sequence (i.e., the sequence that was created first).</p>
                                <p>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p class="center">
                            <strong><span style="font-size: large;">View/export attendees and assigned sequences</span></strong><br>
                            &nbsp;</p>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To see your attendees and their assigned sequence--as well as which attendees have not yet been assigned to a sequence--click the&nbsp;<strong>Attendees</strong> tab on the&nbsp;<strong>Auto-Assign Numbers</strong> screen.&nbsp;</p>
                                <p>
                                    <img width="388" height="184" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/auto-assign-participant-numbers/attendeestab.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    When in the&nbsp;<strong>Attendees</strong> tab, you'll see all your currently registered attendees, their assigned sequence (if any) and assigned number (if any).</p>
                                <p>
                                    You can manually assign a number to an attendee by clicking&nbsp;<strong>Edit</strong>, then inputting the number. If you'd like to remove a previously-assigned number without entering a new number, click&nbsp;<strong>Unassign</strong>.&nbsp;</p>
                                <p>
                                    <img width="609" height="425" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/auto-assign-participant-numbers/attendeestab1.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    You can export to Excel a report of your attendees, their numbers, sequence names and other information at any time by clicking&nbsp;<strong>Export Attendee Numbers</strong>. The report is in .csv format and will have the name of the event and the date/time of export in its title.</p>
