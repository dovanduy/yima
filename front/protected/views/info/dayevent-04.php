                        <div class="block-heading">
                            <h3>iPhone for check-in</h3>
                            <ul>
                                <li>
                                    Check in attendees on your phone</li>
                                <li>
                                    Scan ticket barcodes or check in by name</li>
                                <li>
                                    Scans barcodes from Android and iPhone Eventbrite apps for attendees</li>
                                <li>
                                    Works with multiple devices and/or laptops</li>
                            </ul>
                        </div>

                        <!--step-->
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> Does your team have iPhone and Android devices? Your Android owners can download Entry Manager from <a href="https://play.google.com/store/apps/details?id=com.eventbrite.organizer">Google Play</a>. All check-ins and data will sync seamlessly across both platforms.</p>
                                <p>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
