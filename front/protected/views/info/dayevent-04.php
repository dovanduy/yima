                        <p style="text-align: center;">
                            <span style="font-size: large;"><strong>Getting started</strong></span><br>
                            &nbsp;</p>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    Now that you've created an event and sold tickets, go to the App Store on your iPhone or iPad and search for and download <strong>Eventbrite Entry Manager</strong>, which has this logo:</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <img width="43" height="43" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/easy-entry-iphone-app/entry-manager-ios-logo.png" alt=""></p>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    (Or you can visit <a href="http://www.eventbrite.com/entrymanager">this page</a> for a direct link and a better look at the app.)</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    When you're ready to check in your guests at the door of the event, log in to the app using your Eventbrite account's log-in ID and password. From the list of your live events that's shown, choose the event for which you'd like to check in your guests or view real-time stats.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong><a href="/en_us/t/multiuser-account-access">Multi-user (subuser) access</a> to Entry Manager is set in the admin account by clicking <strong>Account</strong> in the upper right, then clicking <strong>Multi-User Access</strong>, then clicking <strong>Edit</strong> next to a subuser's email address, and, in the <strong>Actions this user can perform </strong>section, checking the box next to <strong>Check in attendees</strong>.</p>
                                <p>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <strong><span style="font-size: large;">View real-time ticket sales and check-in stats</span></strong></p>
                        <div class="step">
                            <div class="stepnumber">
                                <br>
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    To access real-time sales stats for your event, tap <strong>Stats</strong>. <strong>Tickets Sold</strong> displays the number of tickets sold out of the total number of tickets available. <strong>Attendance</strong> displays the total number of attendees that have been checked in across all devices in use.<br>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <strong><span style="font-size: large;">Check in attendees by name</span></strong></p>
                        <div class="step">
                            <div class="stepnumber">
                                <br>
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    To start checking in your attendees, tap <strong>Check In</strong> to view your attendee list. Scroll to find the attendee you're looking for, or use the search bar at the top to look them up. To check in an attendee, simply tap their name, then tap the green <strong>Check In</strong> button. The attendee's name will then gray out. To undo a check-in, tap the name of a checked-in attendee, then tap the red <strong>Undo Check In</strong> button.</p>
                                <p>
                                    The number at the top of the screen reflects the check-in count on that particular device. If all check-in devices are connected to the Internet, you can also get the total directly from our servers on the <strong>Stats</strong> screen.</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <img style="border-width: 0px; border-style: solid; width: 252px; height: 454px;" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/easy-entry-iphone-app/ent-ee-searchlist.png" alt=""></p>
                        <div class="step">
                            <div class="stepnumber">
                                <br>
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    If your event page has <a href="http://help.eventbrite.com/customer/en_us/portal/articles/426190">offline payments</a> enabled and an attendee chose to pay offline, you'll notice a message that lets you know that a payment is due.</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <img style="border-width: 0px; border-style: solid; width: 252px; height: 454px;" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/easy-entry-iphone-app/iphonenotpaid.png" alt=""></p>
                        <p>
                            &nbsp;</p>
                        <p style="text-align: center;">
                            <strong><span style="font-size: large;">Check in attendees using the barcode scanner</span></strong></p>
                        <div class="step">
                            <div class="stepnumber">
                                <br>
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    Tap <strong>Scan</strong> in the bottom tab bar to launch the barcode scanner. The barcode scanner uses the device's camera to detect the barcode. Simply center the barcode inside the viewfinder and wait for the feedback:<strong> valid</strong> (let them in!), <strong>duplicate</strong> (ticket has already been used) or <strong>invalid </strong>(fake!).</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <img style="border-width: 0px; border-style: solid; width: 406px; height: 470px;" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/easy-entry-iphone-app/ent-ee-scanscreen.png" alt=""></p>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    As a reminder, the number at the top of the screen reflects the check-in count on that device only.</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            &nbsp;</p>
                        <p style="text-align: center;">
                            <strong><span style="font-size: large;">Options and other notes</span></strong></p>
                        <div class="step">
                            <div class="stepnumber">
                                <br>
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    If you have multiple ticket types for your event and would like to limit entry to certain ticket types, tap <strong>Options</strong> in the bottom tab bar and then tap<strong> </strong><strong>Ticket Types</strong>. Select the ticket type(s) that you want to be active on that device.</p>
                                <p style="text-align: center;">
                                    <img style="border-width: 0px; border-style: solid; width: 252px; height: 454px;" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/easy-entry-iphone-app/iphoneoptions.png" alt=""></p>
                                <p>
                                    Attendees having other ticket types will be hidden from the attendee list, and their barcodes will scan as <strong>Limited Entry</strong>. This feature would be useful if, for example, you had a separate entrance for your VIP ticket holders.<br>
                                    <br>
                                    If payment is due on a ticket that you scan or tap to check in, you will be prompted to verify that the attendee has paid.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note:</strong> Should you lose your connection to the Internet during check-in, you will receive a warning from the app, but you can continue to check in attendees offline. All your check-ins will automatically sync once your Internet connection is restored.</p>
                                <p>
                                    &nbsp;</p>
                            </div>
