                                <p>
                                    <strong>Note:</strong> Scanning barcodes can severely reduce your battery life. On the iPhone 4, you can scan for up to 4 hours continuously without the LED flash, and for only 2 hours continuously if using the LED flash. On the iPod Touch 4, you can scan continuously for up to 2.5 hours. You can extend your scanning time by purchasing external battery packs from the Apple Store.</p>
