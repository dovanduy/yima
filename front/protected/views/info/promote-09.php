                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>By default, the RSVP option will be turned <strong>off</strong>, and your invitees will only see an <strong>Attend </strong>button when they open your email invite. You must enable the RSVP option for each round of email invites that you send out in order to give your invitees the option to respond <strong>Yes</strong>, <strong>No </strong>or <strong>Maybe</strong>. See below for instructions on how to enable the RSVP option!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    Once you've created and published your event page, click on the title of your event on the <strong>My Events</strong> page. Then select <strong>Create Invitations</strong> from the features menu on the left side of the <strong>Manage</strong> page for that event.&nbsp;</p>
                                <p>
                                    <img border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/track_status_of_invites/manage%20status(1).png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Click on <strong>Customize</strong> at the top of the preview invite.&nbsp;</p>
                                <img width="608" height="419" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/rsvp_responses/editmessage2.png" alt=""><br><p>
                                    In the <strong>Customize Invitations</strong> window, select the <strong>Track Responses with RSVP</strong> option and click <strong>Save</strong>.</p>
                                <p>
                                    <img width="507" height="367" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/rsvp_responses/option.png" alt=""></p>
                                <p>
                                    After you've enabled the RSVP feature as described above, you can further customize, then send, your <a href="/en_us/t/email-invitations">email invites</a>.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    When your invitee receives their email invite, they will have the option to respond <strong>Yes</strong>, <strong>No</strong> or <strong>Maybe.&nbsp;</strong></p>
                                <img width="576" height="504" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/rsvp_responses/invite2.png" alt=""><p>
                                    Once the invitee clicks on their response in the email, they will be taken to your event page, where they will then make their selection--<strong>Yes</strong>, <strong>No</strong> or <strong>Maybe</strong>--and either click <strong>Register</strong> (for free events) or <strong>Order Now </strong>(for paid events) if the response is <strong>Yes</strong>, or <strong>RSVP Maybe </strong>or <strong>RSVP No</strong>. If the attendee chooses No or Maybe, they will have the option to include a message that will be visible only to you.&nbsp;</p>
                                <p>
                                    <img width="601" height="346" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/rsvp_responses/event_page.png" alt=""></p>
