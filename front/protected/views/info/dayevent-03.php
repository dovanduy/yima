                        <div class="block-heading">
                            <h3>Android for check-in</h3>
                            <ul>
                                <li>
                                    Check in attendees on your phone</li>
                                <li>
                                    Scan ticket barcodes or check in by name</li>
                                <li>
                                    Scans barcodes from Android and iPhone Eventbrite apps for attendees</li>
                                <li>
                                    Works with multiple devices and/or laptops</li>
                            </ul>
                        </div>

                        <!--step-->
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> Does your team have Android and iPhone devices? Your iPhone owners can download Entry Manager from Appleâ€™s App Store. All check-ins and data will sync seamlessly across both platforms.</p>
                                <p>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <img style="border-width: 0px; border-style: solid; width: 464px; height: 336px;" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/android-easy-entry-app/hand-long.png" alt=""><br>
                            &nbsp;</p>
                        <p style="text-align: center;">
                            <span style="font-size: large;"><strong>Getting started</strong></span><br>
                            &nbsp;</p>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    Now that you've created an event and sold tickets, go to Google Play on your Android device and search for and download <strong>Eventbrite Entry Manager</strong>, which has this logo:</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <img width="43" height="43" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/android-easy-entry-app/entry-manager-android-logo.png" alt=""></p>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    (Or you can visit <a href="http://www.eventbrite.com/entrymanager">this page</a> for a direct link and a better look at the app.)</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    When you're ready to check in your guests at the door of the event, log in to the app using your Eventbrite account's log-in ID and password. From the list of your live events that's shown, choose the event for which you'd like to check in your guests or view real-time stats.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong><a href="/en_us/t/multiuser-account-access">Multi-user (subuser) access</a> to Entry Manager is set in the admin account by clicking <strong>Account</strong> in the upper right, then clicking <strong>Multi-User Access</strong>, then clicking <strong>Edit</strong> next to a subuser's email address, and, in the <strong>Actions this user can perform </strong>section, checking the box next to <strong>Check in attendees</strong>.</p>
                                <p>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <strong><span style="font-size: large;">View real-time ticket sales and check-in stats</span></strong><br>
                            &nbsp;</p>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    To access real-time sales stats for your event, tap <strong>Stats</strong>. <strong>Tickets Sold</strong> displays the number of tickets sold out of the total number of tickets available. <strong>Attendance</strong> displays the total number of attendees that have been checked in across all devices in use.</p>
                                <p>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <strong><span style="font-size: large;">Check in attendees by name</span></strong><br>
                            &nbsp;</p>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    To start checking in your attendees, tap <strong>Check In</strong> to view your attendee list. Scroll to find the attendee you're looking for, or tap the search button at the bottom to look them up by name. You can also access the search through the menu button as well.</p>
                                <p>
                                    To check in an attendee, simply tap their name, then tap the green <strong>Check In</strong> button that pops up. The attendee's name will then gray out. To undo a check-in, tap the name of a checked-in attendee, then tap the red <strong>Undo Check In</strong> button.</p>
                                <p>
                                    The number at the top of the screen reflects the check-in count on that particular device. If all check-in devices are connected to the Internet, you can get the total attendance directly from our servers on the <strong>Stats</strong> screen.</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <img style="border-width: 0px; border-style: solid; width: 268px; height: 469px;" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/android-easy-entry-app/ent-android-searchlist.png" alt=""><br>
                            &nbsp;</p>
                        <p style="text-align: center;">
                            <strong><span style="font-size: large;">Check in attendees using the barcode scanner</span></strong><br>
                            &nbsp;</p>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    Tap the<strong> 2D barcode icon</strong> in the top action bar to launch the barcode scanner. You can also open the scanner through the Menu button at the bottom. The barcode scanner uses the device's camera to detect the barcode. Simply center the barcode inside the viewfinder and wait for the feedback:<strong> valid</strong> (let them in!), <strong>duplicate</strong> (ticket has already been used) or <strong>invalid </strong>(fake!).</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <img style="border-width: 0px; border-style: solid; width: 472px; height: 371px;" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/android-easy-entry-app/ent-android-scan-options.png" alt=""></p>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    As a reminder, the number at the top of the screen reflects the check-in count on that device only.</p>
                                <p>
                                    &nbsp;</p>
                            </div>
                        </div>
                        <p style="text-align: center;">
                            <strong><span style="font-size: large;">Options and other notes</span></strong><br>
                            &nbsp;</p>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    If you have multiple ticket types for your event and would like to limit entry to certain ticket types, tap the menu button and select <strong>Limit Entry</strong>. Choose the <a href="/en_us/t/ticket-type-creation">ticket type(s)</a> that you want to be active on that device. Attendees having other ticket types will be hidden from the attendee list, and their barcodes will scan as <strong>Limited Entry</strong>. This feature would be useful if, for example, you had a separate entrance for your VIP ticket holders.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note:</strong> Should you lose your connection to the Internet during check-in, you will receive a warning from the app, but you can continue to check in attendees offline. All your check-ins will automatically sync once your Internet connection is restored.</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note:</strong> Scanning barcodes can severely reduce your battery life. Battery life ranges depending on your device and the number of applications running in the background. Performance, particularly with scanning, also ranges significantly depending on the device. For example, the Nexus S will scan a barcode in less than 1 second, but the Droid X can take up to 5 seconds.</p>
                            </div>
                        </div>
