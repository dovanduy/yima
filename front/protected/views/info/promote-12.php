                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>If you're using our <a href="/en_us/t/invitationonly-events">invite-only feature</a> for your event, you will need to send <a href="/en_us/t/email-invitations">email invitations</a> from within Eventbrite, as the invite-only feature is incompatible with our integration with MailChimp.&nbsp;</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To use MailChimp to send invites for an event, first <a href="/en_us/t/event-page-creation">create and publish</a> your event in Eventbrite. Now click on <strong>My Events</strong> at the top of the page, and then click on the name of your published event to go to its <strong>Manage</strong> page. Select <strong>Create Invitations</strong> from the feature menu on the left side of the <strong>Manage</strong> page.</p>
                                <p>
                                    <img border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/integrate-eventbrite-and-mailchimp/create_invitations.png"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Click on the <strong>Use MailChimp!</strong> button on the right side of the next screen. A new tab or window will open, and you will be taken to MailChimp and prompted to log in to your MailChimp account (or to sign up for one if you haven't already).&nbsp;</p>
                                <p>
                                    <img width="607" height="313" border="0" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/integrate-eventbrite-and-mailchimp/use_mailchimp.png"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Go <a target="_blank" href="http://kb.mailchimp.com/article/how-do-i-send-my-eventbrite-event-invitation-through-mailchimp">here </a>to learn what to do in MailChimp from this point on in order to send your invites.&nbsp;</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/en_US/helpcenter/article_note.png" alt="Note"></div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>You may be prompted by MailChimp to enter your Eventbrite API user key if you haven't already.</p>
                                <p>
                                    To find your key, go back to Eventbrite, make sure you're logged in, and click <strong>Account</strong> in the upper right. Then click on <strong>API User Key</strong> on the vertical menu on the left side of the page. You will find your key in the box labeled <strong>Your API User Key </strong>at the top of the next page.</p>
