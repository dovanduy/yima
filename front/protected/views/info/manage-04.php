                            <div class="block-heading">
                                <h3>Help! People can't register for my event</h3>
                                <ul>
                                    <ul>

                                        <li>
                                            Tickets not showing on event page</li>
                                        <li>
                                            "N/A" or "Sold Out" displays next to a ticket type</li>
                                        <li>
                                            Troubleshooting tips to get your tickets on sale</li>

                                    </ul>
                                </ul>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> If people are unable to register for your event, it’s most likely because your tickets are hidden (<a href="#step1">#1 below</a>), your tickets are set to go on sale in the future (<a href="#step2">#2 below</a>), or your tickets are sold out (<a href="#step3">#3 below</a>). Scroll down to learn how to address each of these three scenarios!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <a name="step1"></a>1</div>
                            <div class="stepcontent">
                                <p>
                                    If the <strong>Ticket Information</strong> box is not displayed on your event page, it means that your tickets are hidden. Here's how to show them!</p>
                                <p>
                                    <img width="606" height="239" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event-attendees-unable-to-register/cantreg_1.png" alt=""></p>
                                <p>
                                    From the <strong>My Events</strong> page, click <strong>Edit </strong>to the right of the title of your event. In <strong>Step 2: Create Tickets</strong>, find the <strong>Sales Status </strong>dropdown menu and change the status from <strong>Hidden</strong> to <strong>On Sale</strong>. Click <strong>Save</strong> in the upper right to save your changes.</p>
                                <p>
                                    <img style="border-width: 0px; border-style: solid; width: 607px; height: 177px;" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event-attendees-unable-to-register/cantreg_2.png" alt=""></p>
                                <p>
                                    To verify that your tickets are now on sale, click <strong>Colors &amp; Options</strong> in the upper left to take a look at your event page. The <strong>Ticket Information </strong>box should now be visible and a dropdown menu under <strong>Quantity</strong> showing, meaning that your attendees will be able to register.</p>
                                <p>
                                    <img width="430" height="260" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event-attendees-unable-to-register/cantreg_6.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <a name="step2"></a>2</div>
                            <div class="stepcontent">
                                <p>
                                    If the <strong>Ticket Information</strong> box on your event page displays <strong>N/A</strong> instead of a dropdown menu beneath <strong>Quantity </strong>to the right of the ticket fee, it means that your tickets are not yet on sale. Here's how to adjust the start date of sales to put them on sale!</p>
                                <p>
                                    <img width="433" height="229" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event-attendees-unable-to-register/cantreg_4.png" alt=""></p>
                                <p>
                                    From the <strong>My Events</strong> page, click <strong>Edit</strong> to the right of the title of your event. In <strong>Step 2: Create Tickets</strong>, you’ll see that the <strong>Sales Status </strong>dropdown menu displays <strong>Not Started</strong>.</p>
                                <p>
                                    <img width="611" height="214" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event-attendees-unable-to-register/cantreg_5a.png" alt=""></p>
                                <p>
                                    To change the sales start date, click <strong>Settings </strong>to the right of the<strong> Sales Status </strong>dropdown menu, then adjust the <strong>Sale Starts</strong> date to today, so that you see the <strong>Sales Status </strong>dropdown menu change to <strong>On Sale</strong>. Click <strong>Save </strong>in the upper right to save your changes.</p>
                                <p>
                                    <img width="610" height="295" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event-attendees-unable-to-register/cantreg_5b.png" alt=""></p>
                                <p>
                                    To verify that your tickets are now on sale, click <strong>Colors &amp; Options</strong> in the upper left to take a look at your event page. A dropdown menu should now be showing <strong>Quantity</strong>, meaning that your attendees will be able to register.</p>
                                <p>
                                    <img width="430" height="260" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event-attendees-unable-to-register/cantreg_6.png" alt=""></p>
                            </div>

                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <a name="step3"></a>3</div>
                            <div class="stepcontent">
                                <p>
                                    If the <strong>Ticket Information</strong> box displays <strong>Sold Out</strong> below <strong>Quantity</strong> and the event should not be sold out, you should adjust the <strong>Ticket Quantity</strong> of that particular type of ticket. Here’s how!</p>
                                <p>
                                    <img width="440" height="247" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event-attendees-unable-to-register/cantreg_5cc.png" alt=""></p>
                                <p>
                                    From the <strong>My Events</strong> page, click on <strong>Edit</strong> to the right of your event title. In <strong>Step 2: Create Tickets</strong>, increase the <strong>Ticket Quantity</strong> of the specific ticket type so the <strong>Sales Status </strong>changes from <strong>Sold Out</strong> to <strong>On Sale</strong>.</p>
                                <p>
                                    <img width="604" height="189" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event-attendees-unable-to-register/cantreg_7.png" alt=""></p>
                                <p>
                                    If the <strong>Ticket Quantity </strong>is correct but the ticket is still displaying as <strong>Sold Out</strong>, you may need to increase the <strong>Event Capacity</strong>. (This is the total number of tickets&mdash;across all ticket types&mdash;that can be sold for the event, regardless of the mix in sales of individual ticket types. The <strong>Event Capacity</strong> will always override the <strong>Ticket Quantity</strong> for your ticket types.)</p>
                                <p>
                                    <img width="604" height="211" border="0" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/event-attendees-unable-to-register/cantreg_8.png" alt=""></p>
                                <p>
                                    To verify that your tickets are now on sale, click <strong>Colors &amp; Options</strong> in the upper left to look at your event page. There should now be a dropdown menu displayed under <strong>Quantity</strong>, allowing your attendees to choose how many tickets they would like purchase.</p>
                                <p>
