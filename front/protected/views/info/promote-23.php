                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip: </strong>To first learn how to create / import a contact list into Eventbrite, click <a href="/en_us/t/import-contact-lists">here</a>!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To view and manage your contact lists, click on the <strong>My Contacts</strong> tab located at the top of any page when you're logged in to Eventbrite.</p>
                                <p>
                                    <img src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/importing_contacts/my_contacts2.png" border="0" height="60" width="619"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    On the <strong>My Contacts</strong> page, you'll see all your contact lists, the number of contacts in each list, the date each was created, and the last time that you sent an email invitation from the <strong>My Contacts</strong> page to each list.</p>
                                <p>
                                    In the far right column are four action icons (from left to right): <strong>edit contact list</strong>,<strong> copy contact list</strong>,<strong> delete contact list</strong>,<strong> </strong>and <strong>send invites to this contact list. </strong>You can click the name of a contact list to view all the email addresses in that list and see more options for that list. To search across all your contact lists for a specific contact, use the search box on the far right, labeled <strong>Search across all contacts</strong>.</p>
                                <p>
                                    <img src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/manage_contacts/my_contacts.png" border="0" height="182" width="611"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>To send an email invite to one or more contact lists </strong></p>
                                <p>
                                    Click the check box next to those contact lists and then click <strong>Send Invitations to Selected.</strong> In the <strong>Create Invite</strong> box, select the event for which you would like to send invitations to the contact list(s), then press <strong>Create Invite</strong>.</p>
                                <p>
                                    <img src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/manage_contacts/send_invites.png" border="0" height="296" width="614"></p>
                                <p>
                                    You will be taken to the <strong>Create Invitations</strong> page for the event you selected and your contact list will be auto-loaded into the invite's recipient list.&nbsp; Here you can preview and edit your email invitation, add or remove contacts from the recipient list, and then send the invitation or schedule it to go out in the future.</p>
                                <p>
                                    To learn more about customizing and scheduling / sending your email invitation, go <a href="/en_us/t/email-invitations">here</a>.&nbsp;</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>To view and/or edit the entries in a contact list </strong>click the name of the contact list, which will show you all contacts in that list, as seen below.&nbsp;</p>
                                <p>
                                    <img src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/manage_contacts/contact_list.png" border="0" height="361" width="606"></p>
                                <p>
                                    When looking at an individual contact list, you can:</p>
                                <p>
                                    - <strong>Search for a contact</strong> using the <strong>search box</strong> in the upper right.&nbsp;</p>
                                <p>
                                    - <strong>Add more contacts</strong> by clicking <strong>add more</strong> in the upper left. You can import contacts from a .csv or .txt file, from an outside email account such as Hotmail or Gmail, from attendees of a past event in your Eventbrite account, or by manually entering email addresses.</p>
                                <p>
                                    - <strong>Export contacts to a .csv file </strong>by selecting the contact(s) you want to export, then clicking <strong>Export Selected</strong> near the bottom. You can then import these contacts from the .csv file into an outside email client, suck as Outlook.</p>
                                <p>
                                    - <strong>Delete contacts from the contact list</strong> by selecting the contact(s) you want to delete, then clicking <strong>Delete Selected</strong> near the bottom.</p>
                                <p>
                                    - <strong>Send email invitations to specific contacts</strong> by selecting the contact(s) you want to send invites to, then clicking <strong>Send Invitations to Selected</strong> near the bottom.</p>
                                <p>
                                    - <strong>Copy contacts to another contact list</strong> by selecting the contact(s) you want to copy, then clicking <strong>Copy Selected to Another List</strong>. A <strong>Copy Contacts</strong> window will appear, and you can select the contact list you want to copy to or else create a new contact list with your selected contacts.</p>
