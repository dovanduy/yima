                            <h3>Waitlist management</h3>
                            <ul>
                                <ul>

                                    <li>
                                        Manually release tickets to people on waitlist</li>
                                    <li>
                                        Attendee receives email and clicks through to register</li>
                                    <li>
                                        One ticket per entry on waitlist</li>

                                </ul>
