                        <!--step-->
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    After you have <a href="/en_us/t/waitlist-setup">set up a waitlist</a> for your event, once a ticket type has sold out or the event capacity has been reached, the <b>Add to Waitlist</b> link will appear in the <b>Ticket Information</b> box on your event page.</p>
                                <p>
                                    <img width="617" height="179" border="0" title="Waitlist Management" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/manage_waitlist/ticket%20info.png" alt="Waitlist Management"></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Potential attendees will be able to add themselves to the waitlist by clicking <b>Add to Waitlist</b> and entering their information. Once they have done so, they will see the <a href="/en_us/t/waitlist-setup"><b>Auto-Response Message</b></a> that you have set in <b>Waitlist Settings</b>.</p>
                                <img width="583" height="425" border="0" title="Waitlist Management" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/manage_waitlist/add%20info.png" alt="Waitlist Management">
                            </div>

                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    You will receive a notification email when the first person registers for the waitlist. To manage your waitlist and release tickets, you can click the link in that email to be taken to <b>Manage Waitlist</b>. Or, from the <b>My Events</b> page in your Eventbrite account, click on the title of your event to go to the <b>Manage </b>page for that event. On the <b>Manage </b>page, select <b>Manage Waitlist </b>from the features menu on the left side of the page.</p>
                                <img border="0" title="Waitlist Management" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/manage_waitlist/manage.png" alt="Waitlist Management">
                            </div>

                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    On the <b>Manage Waitlist</b> page, you can view the list of potential attendees who signed up for the waitlist and release tickets to any of these people by checking the box next to a name and clicking <b>Release Tickets</b>.</p>
                                <p>
                                    You can also manually add people to the waitlist by clicking <b>Add People to the Waitlist</b> and filling in the necessary information for each person. &nbsp;</p>
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <b>Tip:</b> Each entry on the waitlist corresponds to one ticket/registration to your event. If you would like to offer a potential attendee on the waitlist more than one ticket, you would need to add them one or more additional times to the waitlist and then release tickets to each of these entries.</p>
                                <img width="609" height="201" border="0" title="Waitlist Management" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/manage_waitlist/waitlist.png" alt="Waitlist Management">
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    When you do release a ticket for the event to someone on the waitlist, the potential attendee will receive a notification email that has a <b>Claim my spot </b>button to that will allow them to register for your event. The potential attendee <b>must </b>click on this button in the notification email in order to be able to buy a ticket/register for your event.</p>
                                <p>
                                    If a potential attendee does not buy a ticket/register within the allotted time that you set in <a href="/en_us/t/waitlist-setup"><b>Waitlist Settings</b></a>, the link in the notification expires and will not allow them to buy a ticket/register. If you want to then release a ticket to the next person on the waitlist, you would have to do so manually.</p>
                                <img width="461" height="596" border="0" title="Waitlist Management" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/manage_waitlist/attendeewaitlist.png" alt="Waitlist Management">
                            </div>
                            <div class="stepnumber">
                                <img src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <b>Note:</b> Ticket sales must be active at the time that the attendee clicks to register for the released waitlist ticket. To adjust the <b>End Sales</b> date/time, click on the name of the ticket type in <b>Step 2: Add Ticket Information</b> on the <b>Edit </b>page of your event.</p>
                            </div>
