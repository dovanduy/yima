                        <div class="block-heading">
                            <h3>Event affiliate programs</h3>
                            <ul>
                                <li>
                                    Turn attendees into promoters</li>
                                <li>
                                    Each affiliate gets a unique link</li>
                                <li>
                                    You and your affiliate can see their performance</li>
                                <li>
                                    Handle payment of your affiliates separate from Eventbrite</li>
                            </ul>
                        </div>

                        <!--step-->
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Tip:</strong> While you can use both <a href="/en_us/t/track-my-promotional-activities">tracking links</a> and affiliate programs to track your promoters' performance, the two features differ in a number of ways. If you use tracking links, you will need to create a unique tracking link for each of your promoters. However, the affiliate program tool allows you to create one program, which all of your promoters can sign up for at a single link Eventbrite provides you after you create the affiliate program. Moreover, an affiliate program also allows your promoters to log in to Eventbrite and track the ticket sales and revenue they've generated for your event, while the performance of a given tracking link is visible only to you, the organizer.&nbsp;</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To get started, go to your event's <strong>Manage</strong> page by clicking on <strong>My Events</strong> at the top of the page, and then clicking on the name of your event. Once on the <strong>Manage </strong>page, select <strong>Create Affiliate Programs</strong> on the features menu on the left side of the page.</p>
                                <img alt="Create an Affiliate Program" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_affiliates/affiliate.png">
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    Enter the <strong>name of your affiliate program</strong>, the <strong>referal fee</strong> you'll pay to an affiliate for each attendee they refer to your event, and any <strong>additional notes</strong> you would like potential affiliates to see in the program details. If you'd like to list your affiliate program in our <a href="http://www.eventbrite.com/publicaffiliates">directory of public affiliate programs</a>, make sure the <strong>Make Public</strong> box is checked. Click <strong>Save</strong> to create your program!</p>
                                <img alt="Create an Affiliate Program" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_affiliates/create_affiliate.png" border="0" height="288" width="590">
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    To promote your affiliate program and get affiliates to sign up, select <strong>Invite Affiliates</strong> from the <strong>Quick Actions</strong> menu next to the name of your affiliate program. You'll then be able to send a customized email invitation for your affiliate program to your contacts.</p>
                                <img alt="Create an Affiliate Program" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_affiliates/invite_affilliates.png" border="0" height="471" width="603">
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepcontent">
                                <p>
                                    When your contacts receive the affiliate-program invitation and click on the link to sign up, they will be prompted to log in to their Eventbrite account (if they already have an account) or to sign up for an Eventbrite account (if they haven't yet created one). Once they log in, they will be on the <a href="https://www.eventbrite.com/referrals-attendees"><strong>Event Affiliate Program</strong></a> screen, which they can access by clicking the <strong>Account</strong> tab in the upper right at any time.</p>
                                <p>
                                    On this screen, they will find their unique <strong>referral link</strong> (shown in the red box below) to send out to their contacts. The affiliate will receive credit for any ticket sales that come through their unique referral link, and can track the performance of their link in the stats shown on this <a href="https://www.eventbrite.com/referrals-attendees"><strong>Event Affiliate Program</strong></a> screen. The affiliate can change their PayPal email address for payout by clicking on the <strong><a href="https://www.eventbrite.com/payment-information"><strong>Payment Accounts</strong></a> </strong>link in the <strong>Account</strong> tab.</p>
                                <img alt="" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_affiliates/account.png" border="0" height="417" width="618">
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    If you, the organizer, would like to view each of your affiliates' performance, select <strong>View Users</strong> from the <strong>Quick Actions</strong> menu located next to any affiliate program listed in the <strong>Create Affiliate Programs</strong> screen for your event. You can also click <strong>Attendee Report</strong> on this <strong>Quick Actions</strong> dropdown menu to view an <a href="/en_us/t/attendance-summary">Attendee Summary report</a> for more details about any attendees who registered for your event through this affiliate program.</p>
                                <img alt="Create an Affiliate Program" src="https://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/create_affiliates/created_affiliate.png" border="0" height="190" width="590">
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img alt="Note" src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" border="0">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note:</strong> You, the event organizer and affiliate-program owner, are responsible for making payment to your affiliates. Payment can be handled via PayPal, but is conducted entirely outside of Eventbrite. We leave the management of your affiliate program(s) up to you!</p>
                            </div>
