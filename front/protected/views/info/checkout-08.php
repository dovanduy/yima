                        <p class="center">
                            <strong><span style="font-size: large;">Create your custom questions and sub-questions</span></strong></p>
                        <div class="step">
                            <div class="stepnumber">
                                1</div>
                            <div class="stepcontent">
                                <p>
                                    To get started, first <a href="http://www.eventbrite.com/signup">sign up</a> for a free Eventbrite account or<a href="http://www.eventbrite.com/login"> log in</a> to your existing Eventbrite account and<a href="/en_us/t/event-page-creation"> create an event</a>.</p>
                                <p>
                                    If youâ€™d like to edit an already-created event, click <strong>My Events</strong> at the top of the screen and go to your event's <strong>Manage</strong> page by clicking on the name of your event.</p>
                                <p>
                                    <img width="615" height="253" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/conditional-logic-for-custom-questions/manage.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                2</div>
                            <div class="stepcontent">
                                <p>
                                    On your event's <strong>Manage</strong> page, click <strong>Customize Order Form</strong>, found on the left side of the page.</p>
                                <p>
                                    <img width="227" height="224" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/conditional-logic-for-custom-questions/features-menu.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                &nbsp;</div>
                            <div class="stepcontent">
                                <p>
                                    Next, choose to&nbsp;<strong>Collect information below for the ticket buyer only</strong> or&nbsp;<strong>Collect information below for each attendee</strong> in the box titled&nbsp;<strong>What type of information do you want to collect?</strong></p>
                                <p>
                                    <img width="610" height="421" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/conditional-logic-for-custom-questions/collect-info-for-buyer-or-each-attendee.png" alt=""></p>
                                <p>
                                    Then click <strong>Create a question</strong>.</p>
                                <p>
                                    <img width="612" height="538" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/conditional-logic-for-custom-questions/create-a-question.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                3</div>
                            <div class="stepcontent">
                                <p>
                                    Enter your <strong>question</strong>, choose the <strong>question type</strong> (you can use conditional logic with checkboxes, radio buttons and dropdowns), and input the answer <strong>options available</strong> for your attendees (along with any associated choice limits, if applicable, in the <strong>Quantity</strong> field).</p>
                                <p>
                                    To create a sub-question, select <strong>Surface a secondary question based on answers to this question</strong>.</p>
                                <p>
                                    <img width="612" height="312" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/conditional-logic-for-custom-questions/conditional-questions-option.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                4</div>
                            <div class="stepcontent">
                                <p>
                                    Choose which answer option will trigger this sub-question, then enter the sub-question. Select the <strong>question type</strong>, and set the <strong>options available</strong> (along with any associated choice limits, if applicable, by clicking <strong>Add choice limit</strong>).</p>
                                <p>
                                    (In our example, an attendee will be asked "At which time would you like to attend?" if they select "Hip hop" as the answer to the original custom question "Which style of dance are you interested in?")</p>
                                <p>
                                    If you'd like to create a second sub-question to your custom question, click <strong>Add another rule</strong>. Otherwise, click <strong>Save</strong> to save your changes.</p>
                                <p>
                                    <img width="612" height="449" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/conditional-logic-for-custom-questions/sub-question-one-and-save.png" alt=""></p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                <img border="0" src="http://ebmedia.eventbrite.com/s3-s3/static/images/helpcenter/article_note.png" alt="Note">
                            </div>
                            <div class="stepcontent">
                                <p>
                                    <strong>Note: </strong>At this time, you can only create a sub-question for a custom question, not for another sub-question. However, you can create as many sub-questions for a custom question as you'd like!</p>
                            </div>
                        </div>
                        <div class="step">
                            <div class="stepnumber">
                                5</div>
                            <div class="stepcontent">
                                <p>
                                    Back on the main&nbsp;<strong>Customize order form</strong> page, you'll see your custom questions, along with any sub-questions (and their answers) that you've created. Here's where you can choose to make a custom question or sub-question required, or delete a custom question or sub-question.</p>
                                <p>
                                    To add another custom question, simply click <strong>Add another question</strong>.</p>
                                <p>
                                    <img width="613" height="353" src="http://evbdn.eventbrite.com/s3-s3/static/images/en_US/tutorials/conditional-logic-for-custom-questions/your-questions.png" alt=""></p>
                                <p>
                                    &nbsp;</p>
